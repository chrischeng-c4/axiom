use crate::cli::update::is_newer;

// Run all applicable config migrations based on version.
// Returns (migrated_content, list_of_applied_migration_names).
// @spec projects/agentic-workflow/tech-design/surface/interfaces/src/migrate.md#source
pub fn migrate_config(content: &str, from_version: &str) -> (String, Vec<String>) {
    let mut result = content.to_string();
    let mut applied = Vec::new();

    // Migration: envfile support (added in 0.3.2)
    if version_lt(from_version, "0.3.2") {
        if let Some(migrated) = migrate_envfile_support(&result) {
            result = migrated;
            applied.push("envfile support".into());
        }
    }

    // Migration: [project] section with modules (added in 0.3.3)
    if version_lt(from_version, "0.3.3") {
        if let Some(migrated) = migrate_project_section(&result) {
            result = migrated;
            applied.push("project modules".into());
        }
    }

    (result, applied)
}

// Returns true if `version` is strictly less than `target`.
fn version_lt(version: &str, target: &str) -> bool {
    // is_newer(a, b) returns true when a > b
    is_newer(target, version)
}

// Insert commented-out envfile lines into config.toml.
// Returns None if envfile already present (idempotent).
fn migrate_envfile_support(content: &str) -> Option<String> {
    // Skip if envfile is already mentioned anywhere
    if content.contains("envfile") {
        return None;
    }

    let mut result = content.to_string();

    // 1) Insert global envfile comment before [workflow.agents]
    result = insert_before(
        &result,
        "[workflow.agents]",
        "# Global envfile - loaded for all agents (relative to project root or absolute)\n\
         # envfile = \".env\"\n\n",
    );

    // 2) Insert provider-specific envfile after `default = "..."` in each provider section
    result = insert_provider_envfile(&result, "[gemini]", ".gemini.env");
    result = insert_provider_envfile(&result, "[codex]", ".codex.env");
    result = insert_provider_envfile(&result, "[claude]", ".claude.env");

    Some(result)
}

// Insert `text` immediately before `anchor` in `content`.
// If anchor is not found, returns content unchanged.
fn insert_before(content: &str, anchor: &str, text: &str) -> String {
    if let Some(pos) = content.find(anchor) {
        let mut result = String::with_capacity(content.len() + text.len());
        result.push_str(&content[..pos]);
        result.push_str(text);
        result.push_str(&content[pos..]);
        result
    } else {
        content.to_string()
    }
}

// Insert a commented envfile line after the `default = "..."` line within a provider section.
fn insert_provider_envfile(content: &str, section_header: &str, envfile_name: &str) -> String {
    let Some(section_start) = content.find(section_header) else {
        return content.to_string();
    };

    let after_section = &content[section_start..];

    // Find the `default = ` line within this section
    let Some(default_offset) = after_section.find("default = ") else {
        return content.to_string();
    };

    // Find end of that line
    let abs_default_pos = section_start + default_offset;
    let after_default = &content[abs_default_pos..];
    let line_end = after_default.find('\n').unwrap_or(after_default.len());
    let insert_pos = abs_default_pos + line_end + 1; // after the newline

    let comment = format!(
        "# envfile = \"{}\"  # provider-specific env (overrides global)\n",
        envfile_name
    );

    let mut result = String::with_capacity(content.len() + comment.len());
    result.push_str(&content[..insert_pos]);
    result.push_str(&comment);
    result.push_str(&content[insert_pos..]);
    result
}

// Insert commented-out [project] section at the top of config.
// Returns None if project section already present (idempotent).
fn migrate_project_section(content: &str) -> Option<String> {
    // Skip if [project] or [[project.modules]] is already mentioned
    if content.contains("[project]") || content.contains("[[project.modules]]") {
        return None;
    }

    let project_section = "\
# Project modules - map directories to languages (monorepo-aware)
# [[project.modules]]
# path = \".\"
# language = \"rust\"
# framework = \"axum\"

";

    // Insert at the very beginning (before everything else)
    let mut result = String::with_capacity(content.len() + project_section.len());
    result.push_str(project_section);
    result.push_str(content);
    Some(result)
}

#[cfg(test)]
mod tests {
    use super::*;

    /// Config generated by toml::to_string_pretty() on pre-0.3.2 versions
    const OLD_CONFIG: &str = r#"[workflow.agents]
explore_spec = [
    "gemini:flash",
    "mainthread",
]
implement = ["mainthread"]

[gemini]
command = "gemini"
default = "flash"

[[gemini.models]]
id = "flash"
model = "gemini-3-flash-preview"

[codex]
command = "codex"
default = "balanced"

[[codex.models]]
id = "balanced"
model = "gpt-5.3-codex"

[claude]
command = "claude"
default = "balanced"

[[claude.models]]
id = "balanced"
model = "sonnet"

[validation]
required_headings = ["Overview"]
"#;

    #[test]
    fn test_migrate_envfile_from_old_version() {
        let (result, applied) = migrate_config(OLD_CONFIG, "0.3.1");
        assert_eq!(applied, vec!["envfile support", "project modules"]);

        // Global envfile comment before [workflow.agents]
        assert!(result.contains("# envfile = \".env\"\n\n[workflow.agents]"));

        // Provider-specific envfile comments
        assert!(result.contains("default = \"flash\"\n# envfile = \".gemini.env\""));
        assert!(result.contains("default = \"balanced\"\n# envfile = \".codex.env\""));
        // Claude also has default = "balanced", but it's in [claude] section
        assert!(result.contains("default = \"balanced\"\n# envfile = \".claude.env\""));
    }

    #[test]
    fn test_migrate_idempotent() {
        let (first_pass, applied1) = migrate_config(OLD_CONFIG, "0.3.1");
        assert!(!applied1.is_empty());

        let (second_pass, applied2) = migrate_config(&first_pass, "0.3.1");
        assert!(
            applied2.is_empty(),
            "Should not apply envfile migration twice"
        );
        assert_eq!(first_pass, second_pass);
    }

    #[test]
    fn test_migrate_skips_current_version() {
        // 0.3.3 is the latest migration version, so nothing should apply
        let (result, applied) = migrate_config(OLD_CONFIG, "0.3.3");
        assert!(applied.is_empty());
        assert_eq!(result, OLD_CONFIG);
    }

    #[test]
    fn test_migrate_skips_newer_version() {
        let (result, applied) = migrate_config(OLD_CONFIG, "1.0.0");
        assert!(applied.is_empty());
        assert_eq!(result, OLD_CONFIG);
    }

    #[test]
    fn test_migrate_from_unknown_version() {
        // "unknown" is not valid semver, version_lt falls back to string comparison
        // is_newer("0.3.2", "unknown") will fail to parse "unknown" -> returns true
        // So version_lt("unknown", "0.3.2") returns true -> all migrations run
        // That's fine - "unknown" means very old version
        let (_result, applied) = migrate_config(OLD_CONFIG, "unknown");
        assert_eq!(applied, vec!["envfile support", "project modules"]);
    }

    #[test]
    fn test_migrate_envfile_already_present() {
        let config_with_envfile = format!("envfile = \".env\"\n\n{}", OLD_CONFIG);
        let result = migrate_envfile_support(&config_with_envfile);
        assert!(result.is_none(), "Should skip if envfile already in config");
    }

    #[test]
    fn test_provider_envfile_placement() {
        let (result, _) = migrate_config(OLD_CONFIG, "0.3.0");

        // Check gemini section
        let gemini_pos = result.find("[gemini]").unwrap();
        let gemini_envfile_pos = result.find("# envfile = \".gemini.env\"").unwrap();
        let gemini_models_pos = result.find("[[gemini.models]]").unwrap();
        assert!(
            gemini_envfile_pos > gemini_pos,
            "envfile should be after [gemini]"
        );
        assert!(
            gemini_envfile_pos < gemini_models_pos,
            "envfile should be before [[gemini.models]]"
        );

        // Check codex section
        let codex_pos = result.find("[codex]").unwrap();
        let codex_envfile_pos = result.find("# envfile = \".codex.env\"").unwrap();
        let codex_models_pos = result.find("[[codex.models]]").unwrap();
        assert!(codex_envfile_pos > codex_pos);
        assert!(codex_envfile_pos < codex_models_pos);

        // Check claude section
        let claude_pos = result.find("[claude]").unwrap();
        let claude_envfile_pos = result.find("# envfile = \".claude.env\"").unwrap();
        let claude_models_pos = result.find("[[claude.models]]").unwrap();
        assert!(claude_envfile_pos > claude_pos);
        assert!(claude_envfile_pos < claude_models_pos);
    }

    #[test]
    fn test_migrate_project_section() {
        let (result, applied) = migrate_config(OLD_CONFIG, "0.3.2");
        assert_eq!(applied, vec!["project modules"]);
        assert!(result.contains("[[project.modules]]"));
        // Should be at the top
        assert!(result.starts_with("# Project modules"));
    }

    #[test]
    fn test_migrate_project_section_idempotent() {
        let (first_pass, _) = migrate_config(OLD_CONFIG, "0.3.2");
        let (second_pass, applied2) = migrate_config(&first_pass, "0.3.2");
        assert!(applied2.is_empty());
        assert_eq!(first_pass, second_pass);
    }

    #[test]
    fn test_migrate_project_section_skips_if_present() {
        let with_project = format!(
            "[[project.modules]]\npath = \".\"\nlanguage = \"rust\"\n\n{}",
            OLD_CONFIG
        );
        let result = migrate_project_section(&with_project);
        assert!(result.is_none());
    }

    #[test]
    fn test_migrate_all_from_old_version() {
        // From very old version: should apply both envfile and project migrations
        let (result, applied) = migrate_config(OLD_CONFIG, "0.3.0");
        assert_eq!(applied, vec!["envfile support", "project modules"]);
        assert!(result.contains("envfile"));
        assert!(result.contains("[[project.modules]]"));
    }
}


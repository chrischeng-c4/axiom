/// @spec projects/agentic-workflow/tech-design/core/generate/generators/sequence_plus_gen_runtime.md#source
impl SpecIRGenerator for SequencePlusGenerator {
    fn can_generate(&self, spec: &SpecIR) -> bool {
        matches!(spec, SpecIR::SequencePlus { .. })
    }

    fn template_dir(&self) -> &'static str {
        "sequence_plus"
    }

    fn generate_from_ir(
        &self,
        spec: &SpecIR,
        settings: &GeneratorSettings,
        engine: &TemplateEngine,
    ) -> Result<Manifest, GeneratorError> {
        let seq_def = match spec {
            SpecIR::SequencePlus { def, .. } => def,
            _ => {
                return Err(GeneratorError::SchemaError(
                    "SequencePlusGenerator: expected SpecIR::SequencePlus variant".into(),
                ))
            }
        };

        let mut manifest = Manifest::new();

        let file_name = format!("{}_handlers.py", seq_def.id.replace('-', "_"));
        let output_path = settings.output_dir.join(&file_name);

        if output_path.exists() {
            match settings.overwrite_policy {
                OverwritePolicy::Error => {
                    return Err(GeneratorError::OverwriteNotAllowed(output_path));
                }
                OverwritePolicy::Skip => {
                    manifest.add(GeneratedFile::skipped(output_path));
                    return Ok(manifest);
                }
                OverwritePolicy::Overwrite => {}
            }
        }

        let template_name = format!("{}/handlers.py.j2", self.template_dir());
        let content = if engine.has_template(&template_name) {
            engine.render(&template_name, &seq_def).map_err(|e| {
                GeneratorError::TemplateRenderError {
                    template: template_name.clone(),
                    message: e.to_string(),
                }
            })?
        } else {
            generate_sequence_python(seq_def)
        };

        manifest.add(GeneratedFile::written(output_path, &content));
        Ok(manifest)
    }
}


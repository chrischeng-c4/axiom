fn to_snake(s: &str) -> String {
    use heck::ToSnakeCase;
    s.to_snake_case()
}

/// Group consecutive messages that originate from the same participant into
/// handler functions. Each "request" message (solid arrow) from a participant
/// to another participant becomes an async operation inside the handler.
fn generate_sequence_python(def: &SequenceDef) -> String {
    let mut output = String::from("# Generated by sdd\nfrom typing import Any\n\n");

    if def.messages.is_empty() {
        output.push_str("# No messages defined in sequence diagram\n");
        return output;
    }

    // Group messages into "conversations" initiated by the first sender.
    // Each group of outbound messages from one participant becomes an async handler.
    let handlers = group_into_handlers(&def.messages);

    for handler in &handlers {
        let fn_name = to_snake(&handler.name);
        let request_type = format!("{}Request", handler.name_pascal());
        let response_type = "Response".to_string();

        output.push_str(&format!(
            "async def handle_{}(request: {}) -> {}:\n",
            fn_name, request_type, response_type,
        ));

        for step in &handler.steps {
            match step {
                HandlerStep::AwaitCall { target, message } => {
                    let target_snake = to_snake(target);
                    let msg_snake = to_snake(message);
                    output.push_str(&format!(
                        "    # @sdd:implement {}\n    result = await {}.{}(request)\n",
                        msg_snake, target_snake, msg_snake,
                    ));
                }
                HandlerStep::Marker { label } => {
                    output.push_str(&format!("    # @sdd:implement {}\n", to_snake(label),));
                }
                HandlerStep::ReturnResponse { message } => {
                    output.push_str(&format!(
                        "    # @sdd:implement {}\n    return Response(status=200, body=result)\n",
                        to_snake(message),
                    ));
                }
            }
        }

        // If no return was generated, add a default
        if !handler
            .steps
            .iter()
            .any(|s| matches!(s, HandlerStep::ReturnResponse { .. }))
        {
            output.push_str("    return Response(status=200)\n");
        }

        output.push('\n');
    }

    output
}

// ---------------------------------------------------------------------------
// Handler grouping
// ---------------------------------------------------------------------------

struct Handler {
    name: String,
    steps: Vec<HandlerStep>,
}

/// @spec projects/agentic-workflow/tech-design/core/generate/generators/sequence_plus_gen_runtime.md#source
impl Handler {
    fn name_pascal(&self) -> String {
        use heck::ToPascalCase;
        self.name.to_pascal_case()
    }
}

enum HandlerStep {
    /// An async call to another participant
    AwaitCall { target: String, message: String },
    /// A processing marker
    #[allow(dead_code)]
    Marker { label: String },
    /// Return a response
    ReturnResponse { message: String },
}

/// Group messages into handler functions.
///
/// Strategy: Walk through messages. When we see a solid arrow (request)
/// from participant A to participant B, start or continue a handler for A.
/// When we see a dotted arrow (response) back, add a return step.
fn group_into_handlers(messages: &[MessageDef]) -> Vec<Handler> {
    let mut handlers: Vec<Handler> = Vec::new();
    let mut current_handler: Option<Handler> = None;
    let mut current_initiator: Option<String> = None;

    for msg in messages {
        let is_response = matches!(msg.arrow_type, ArrowType::Dotted | ArrowType::DottedOpen);

        if !is_response {
            // Outgoing request
            if current_initiator.as_deref() != Some(&msg.from) {
                // Start new handler group
                if let Some(h) = current_handler.take() {
                    handlers.push(h);
                }
                current_handler = Some(Handler {
                    name: msg.text.clone(),
                    steps: Vec::new(),
                });
                current_initiator = Some(msg.from.clone());
            }

            if let Some(ref mut h) = current_handler {
                h.steps.push(HandlerStep::AwaitCall {
                    target: msg.to.clone(),
                    message: msg.text.clone(),
                });
            }
        } else {
            // Response arrow
            if let Some(ref mut h) = current_handler {
                h.steps.push(HandlerStep::ReturnResponse {
                    message: msg.text.clone(),
                });
            }
        }
    }

    if let Some(h) = current_handler {
        handlers.push(h);
    }

    handlers
}


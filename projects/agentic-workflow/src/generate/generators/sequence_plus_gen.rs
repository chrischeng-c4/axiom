#[cfg(test)]
mod tests {
    use super::*;
    use crate::generate::diagrams::{
        ArrowType, MessageDef, ParticipantDef, ParticipantType, SequenceDef,
    };
    use crate::generate::spec_ir::{SpecIR, SpecMetadata};
    use std::collections::HashMap;

    fn create_artifact_spec() -> SpecIR {
        let mut participants = HashMap::new();
        participants.insert(
            "client".to_string(),
            ParticipantDef {
                label: "Client".into(),
                participant_type: ParticipantType::Actor,
                description: None,
            },
        );
        participants.insert(
            "api".to_string(),
            ParticipantDef {
                label: "API Server".into(),
                participant_type: ParticipantType::Participant,
                description: None,
            },
        );
        participants.insert(
            "repository".to_string(),
            ParticipantDef {
                label: "Repository".into(),
                participant_type: ParticipantType::Participant,
                description: None,
            },
        );

        let messages = vec![
            MessageDef {
                from: "client".into(),
                to: "api".into(),
                text: "create_artifact".into(),
                arrow_type: ArrowType::Solid,
                activate: true,
                deactivate: false,
                description: None,
            },
            MessageDef {
                from: "api".into(),
                to: "repository".into(),
                text: "store_data".into(),
                arrow_type: ArrowType::Solid,
                activate: true,
                deactivate: false,
                description: None,
            },
            MessageDef {
                from: "repository".into(),
                to: "api".into(),
                text: "artifact_stored".into(),
                arrow_type: ArrowType::Dotted,
                activate: false,
                deactivate: true,
                description: None,
            },
            MessageDef {
                from: "api".into(),
                to: "client".into(),
                text: "201 Created".into(),
                arrow_type: ArrowType::Dotted,
                activate: false,
                deactivate: true,
                description: None,
            },
        ];

        SpecIR::SequencePlus {
            def: SequenceDef {
                id: "create-artifact".into(),
                title: Some("Create Artifact Flow".into()),
                participants,
                messages,
                loops: vec![],
                alts: vec![],
                notes: vec![],
                description: None,
            },
            metadata: SpecMetadata::default(),
        }
    }

    #[test]
    fn test_can_generate_sequence() {
        let gen = SequencePlusGenerator::new();
        assert!(gen.can_generate(&create_artifact_spec()));
    }

    #[test]
    fn test_cannot_generate_non_sequence() {
        use crate::generate::schema::JsonSchema;
        let gen = SequencePlusGenerator::new();
        let api_spec = SpecIR::Api {
            schema: JsonSchema::default(),
            metadata: SpecMetadata::default(),
        };
        assert!(!gen.can_generate(&api_spec));
    }

    #[test]
    fn test_generate_produces_one_file() {
        let spec = create_artifact_spec();
        let settings = GeneratorSettings {
            output_dir: std::path::PathBuf::from("/tmp/test_seq_gen"),
            ..Default::default()
        };
        let engine = crate::generate::engine::TemplateEngine::empty();
        let gen = SequencePlusGenerator::new();
        let manifest = gen.generate_from_ir(&spec, &settings, &engine).unwrap();

        assert_eq!(manifest.files.len(), 1);
        let name = manifest
            .files
            .keys()
            .next()
            .unwrap()
            .file_name()
            .unwrap()
            .to_string_lossy()
            .into_owned();
        assert_eq!(name, "create_artifact_handlers.py");
    }

    #[test]
    fn test_generated_python_content() {
        let def = match &create_artifact_spec() {
            SpecIR::SequencePlus { def, .. } => def.clone(),
            _ => unreachable!(),
        };
        let content = generate_sequence_python(&def);

        // Should have async handler
        assert!(content.contains("async def handle_"));
        // Should have await calls
        assert!(content.contains("await"));
        // Should have @sdd:implement markers
        assert!(content.contains("# @sdd:implement"));
        // Should have Response return
        assert!(content.contains("return Response("));
    }

    #[test]
    fn test_empty_sequence() {
        let def = SequenceDef {
            id: "empty".into(),
            title: None,
            participants: HashMap::new(),
            messages: vec![],
            loops: vec![],
            alts: vec![],
            notes: vec![],
            description: None,
        };
        let content = generate_sequence_python(&def);
        assert!(content.contains("No messages defined"));
    }
}

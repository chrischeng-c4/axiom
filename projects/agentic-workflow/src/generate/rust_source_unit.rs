//! rust-source-unit IR: lossless-CST parse to a structured item-tree, with
//! byte-identical emit. This is the codegen primitive that lets arbitrary Rust
//! source units move off brittle source-replay onto td_ast: a TD stores the
//! structured item-tree, an unchanged item re-emits its lossless CST (byte for
//! byte), and an edited item re-renders — so editing the TD regenerates
//! correctly and surgically instead of replaying a stale whole-file snapshot.
//!
//! @spec projects/agentic-workflow/tech-design/validate/rust-source-unit-ir-lossless-cst-parse-to-structured-item-tree-b.md#logic
//!
//! Bootstrap note: this module IS the rust-source-unit generator, so it cannot
//! be produced by the codegen pipeline it implements. It is therefore wrapped
//! in a single HANDWRITE block tracked by issue #40; the gap retires only if a
//! more primitive generator can ever emit this file, which is circular by
//! construction.

// <HANDWRITE gap="bootstrap-generator:rust-source-unit" tracker="40" reason="rust-source-unit is the lossless-CST td_ast codegen primitive itself; it cannot be generated by the pipeline it implements (bootstrap), so the whole module is hand-written against the #logic contract">
use ra_ap_syntax::{
    ast, ast::HasName, ast::HasVisibility, AstNode, AstToken, Edition, NodeOrToken, SourceFile,
};

/// Structured kind of a top-level Rust item, recovered from the CST node.
///
/// `Other` carries the raw `SyntaxKind` debug name so an unrecognised node
/// still round-trips byte-for-byte (its lossless text is preserved); it simply
/// is not given first-class structured handling yet.
///
/// @spec projects/agentic-workflow/tech-design/validate/rust-source-unit-ir-lossless-cst-parse-to-structured-item-tree-b.md#logic
#[derive(Debug, Clone, PartialEq, Eq, serde::Serialize, serde::Deserialize)]
#[serde(tag = "kind", content = "raw", rename_all = "snake_case")]
pub enum ItemKind {
    Fn,
    Struct,
    Enum,
    Union,
    Trait,
    TraitAlias,
    Impl,
    Use,
    ExternCrate,
    ExternBlock,
    Mod,
    Const,
    Static,
    TypeAlias,
    MacroCall,
    MacroRules,
    MacroDef,
    Other(String),
}

/// One structured top-level item segment.
///
/// `text` is the lossless CST text of the item (its byte-source for emit, doc
/// comments and attributes included). `kind`/`name`/`visibility`/`doc`/`attrs`
/// are the structured projection used for inspection and surgical editing; they
/// are never consulted when emitting an unchanged item, so a byte-identical
/// round-trip never depends on perfect structured recovery.
///
/// @spec projects/agentic-workflow/tech-design/validate/rust-source-unit-ir-lossless-cst-parse-to-structured-item-tree-b.md#logic
#[derive(Debug, Clone, PartialEq, Eq, serde::Serialize, serde::Deserialize)]
pub struct ItemNode {
    pub kind: ItemKind,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub name: Option<String>,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub visibility: Option<String>,
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub doc: Vec<String>,
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub attrs: Vec<String>,
    pub text: String,
}

/// One segment of the ordered item-tree: either a structured `Item` or verbatim
/// inter-item `Trivia` (whitespace and free-standing comments). Concatenating
/// every segment's source text reproduces the original file byte-for-byte.
///
/// @spec projects/agentic-workflow/tech-design/validate/rust-source-unit-ir-lossless-cst-parse-to-structured-item-tree-b.md#logic
#[derive(Debug, Clone, PartialEq, Eq, serde::Serialize, serde::Deserialize)]
#[serde(tag = "segment", rename_all = "snake_case")]
pub enum Segment {
    Item(ItemNode),
    Trivia { text: String },
}

impl Segment {
    /// The byte-source of this segment used by [`RustSourceUnit::emit`].
    fn source(&self) -> &str {
        match self {
            Segment::Item(item) => &item.text,
            Segment::Trivia { text } => text,
        }
    }
}

/// A whole Rust source unit as an ordered list of `[Item | Trivia]` segments —
/// the td_ast IR. Editable and serde-stable, yet byte-identical on emit when
/// unchanged.
///
/// @spec projects/agentic-workflow/tech-design/validate/rust-source-unit-ir-lossless-cst-parse-to-structured-item-tree-b.md#logic
#[derive(Debug, Clone, PartialEq, Eq, Default, serde::Serialize, serde::Deserialize)]
pub struct RustSourceUnit {
    pub segments: Vec<Segment>,
}

/// The unit is not a clean Rust source unit: `ra_ap_syntax` reported parse
/// errors. We refuse to build an IR from a file we cannot losslessly model,
/// rather than silently emit a snapshot.
///
/// @spec projects/agentic-workflow/tech-design/validate/rust-source-unit-ir-lossless-cst-parse-to-structured-item-tree-b.md#logic
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ParseError {
    /// One human-readable diagnostic per syntax error (with text offset).
    pub errors: Vec<String>,
}

impl std::fmt::Display for ParseError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(
            f,
            "rust-source-unit parse rejected: {} syntax error(s): {}",
            self.errors.len(),
            self.errors.join("; ")
        )
    }
}

impl std::error::Error for ParseError {}

/// Parse Rust source text into the structured item-tree IR.
///
/// Rejects with [`ParseError`] when the text is not a clean Rust unit (any
/// `ra_ap_syntax` parse error), per the `check` gate in the #logic contract.
/// On success the top level is walked in order: `Node` children become
/// structured [`Segment::Item`]s, all other tokens (whitespace, comments)
/// become [`Segment::Trivia`] captured verbatim.
///
/// @spec projects/agentic-workflow/tech-design/validate/rust-source-unit-ir-lossless-cst-parse-to-structured-item-tree-b.md#logic
pub fn parse(text: &str) -> Result<RustSourceUnit, ParseError> {
    let parsed = SourceFile::parse(text, Edition::Edition2021);
    let errors = parsed.errors();
    if !errors.is_empty() {
        return Err(ParseError {
            errors: errors
                .iter()
                .map(|e| format!("{} @ {:?}", e, e.range()))
                .collect(),
        });
    }

    let file = parsed.tree();
    let mut segments = Vec::new();
    for child in file.syntax().children_with_tokens() {
        match child {
            NodeOrToken::Node(node) => {
                segments.push(Segment::Item(extract_item(&node)));
            }
            NodeOrToken::Token(token) => {
                segments.push(Segment::Trivia {
                    text: token.text().to_string(),
                });
            }
        }
    }

    Ok(RustSourceUnit { segments })
}

impl RustSourceUnit {
    /// Emit the unit back to source text. Concatenates each segment's byte-source
    /// in order: unchanged items emit their lossless CST text (byte-identical to
    /// the original), edited items emit their re-rendered `text`. For an
    /// unmodified unit, `parse(s).emit() == s` byte-for-byte.
    ///
    /// @spec projects/agentic-workflow/tech-design/validate/rust-source-unit-ir-lossless-cst-parse-to-structured-item-tree-b.md#logic
    pub fn emit(&self) -> String {
        let mut out = String::new();
        for segment in &self.segments {
            out.push_str(segment.source());
        }
        out
    }

    /// Structured items only, in source order (trivia elided). Convenience for
    /// inspection and dispatch; does not affect emit.
    pub fn items(&self) -> impl Iterator<Item = &ItemNode> {
        self.segments.iter().filter_map(|s| match s {
            Segment::Item(item) => Some(item),
            Segment::Trivia { .. } => None,
        })
    }
}

/// Recover the structured projection of a top-level CST node. `text` is always
/// the node's lossless text; the remaining fields are best-effort structured
/// metadata that never gate byte-identical emit.
///
/// @spec projects/agentic-workflow/tech-design/validate/rust-source-unit-ir-lossless-cst-parse-to-structured-item-tree-b.md#logic
fn extract_item(node: &ra_ap_syntax::SyntaxNode) -> ItemNode {
    let (kind, name, visibility) = classify(node);
    ItemNode {
        kind,
        name,
        visibility,
        doc: doc_comments(node),
        attrs: outer_attrs(node),
        text: node.text().to_string(),
    }
}

/// Classify a node into `(kind, name, visibility)`. Each arm casts to the
/// matching `ast` item type to read its `name()`/`visibility()`; unrecognised
/// nodes fall through to `Other(<SyntaxKind>)` and still round-trip via `text`.
///
/// @spec projects/agentic-workflow/tech-design/validate/rust-source-unit-ir-lossless-cst-parse-to-structured-item-tree-b.md#logic
fn classify(node: &ra_ap_syntax::SyntaxNode) -> (ItemKind, Option<String>, Option<String>) {
    macro_rules! named {
        ($ty:ty, $kind:expr) => {
            if let Some(it) = <$ty>::cast(node.clone()) {
                return ($kind, name_of(it.name()), vis_of(it.visibility()));
            }
        };
    }
    named!(ast::Fn, ItemKind::Fn);
    named!(ast::Struct, ItemKind::Struct);
    named!(ast::Enum, ItemKind::Enum);
    named!(ast::Union, ItemKind::Union);
    named!(ast::Trait, ItemKind::Trait);
    named!(ast::TraitAlias, ItemKind::TraitAlias);
    named!(ast::Module, ItemKind::Mod);
    named!(ast::Const, ItemKind::Const);
    named!(ast::Static, ItemKind::Static);
    named!(ast::TypeAlias, ItemKind::TypeAlias);

    // Items without a HasName/HasVisibility-shaped name we still recognise.
    if ast::Impl::cast(node.clone()).is_some() {
        return (ItemKind::Impl, None, None);
    }
    if let Some(u) = ast::Use::cast(node.clone()) {
        return (ItemKind::Use, None, vis_of(u.visibility()));
    }
    if let Some(ec) = ast::ExternCrate::cast(node.clone()) {
        return (ItemKind::ExternCrate, None, vis_of(ec.visibility()));
    }
    if ast::ExternBlock::cast(node.clone()).is_some() {
        return (ItemKind::ExternBlock, None, None);
    }
    if ast::MacroCall::cast(node.clone()).is_some() {
        return (ItemKind::MacroCall, None, None);
    }
    if let Some(mr) = ast::MacroRules::cast(node.clone()) {
        return (ItemKind::MacroRules, name_of(mr.name()), None);
    }
    if let Some(md) = ast::MacroDef::cast(node.clone()) {
        return (ItemKind::MacroDef, name_of(md.name()), vis_of(md.visibility()));
    }

    (ItemKind::Other(format!("{:?}", node.kind())), None, None)
}

/// Outer attributes (`#[...]`) attached to the item, in source order.
fn outer_attrs(node: &ra_ap_syntax::SyntaxNode) -> Vec<String> {
    use ast::HasAttrs;
    match ast::AnyHasAttrs::cast(node.clone()) {
        Some(any) => any
            .attrs()
            .map(|a| a.syntax().text().to_string())
            .collect(),
        None => Vec::new(),
    }
}

/// Doc-comment lines (`///` / `//!`) attached to the item, in source order.
fn doc_comments(node: &ra_ap_syntax::SyntaxNode) -> Vec<String> {
    use ast::HasDocComments;
    match ast::AnyHasDocComments::cast(node.clone()) {
        Some(any) => any
            .doc_comments()
            .map(|c| c.syntax().text().to_string())
            .collect::<Vec<_>>(),
        None => Vec::new(),
    }
}

fn name_of(name: Option<ast::Name>) -> Option<String> {
    name.map(|n| n.syntax().text().to_string())
}

fn vis_of(vis: Option<ast::Visibility>) -> Option<String> {
    vis.map(|v| v.syntax().text().to_string())
}

#[cfg(test)]
mod tests {
    use super::*;

    /// Helper: parse then emit, asserting a byte-identical round-trip.
    fn assert_round_trip(src: &str) {
        let unit = parse(src).expect("clean source must parse");
        assert_eq!(unit.emit(), src, "emit must be byte-identical to input");
    }

    #[test]
    fn empty_file_round_trips() {
        assert_round_trip("");
    }

    #[test]
    fn whitespace_only_round_trips() {
        assert_round_trip("\n\n   \n");
    }

    #[test]
    fn comments_and_items_round_trip_byte_identical() {
        let src = "\
//! module doc
//! second line

use std::collections::HashMap; // trailing comment

/// A doc-commented, attributed struct.
#[derive(Debug, Clone)]
pub struct Foo {
    pub a: u32,
    b: String,
}

// free-standing comment between items

impl Foo {
    fn new() -> Self {
        Foo { a: 0, b: String::new() }
    }
}
";
        assert_round_trip(src);
    }

    #[test]
    fn parse_rejects_syntactically_broken_source() {
        let err = parse("fn broken( {").expect_err("broken source must be rejected");
        assert!(!err.errors.is_empty(), "must report at least one error");
    }

    #[test]
    fn structured_projection_recovers_kinds_names_and_visibility() {
        let src = "\
use std::fmt;

pub struct Bar {
    x: i64,
}

enum Color {
    Red,
    Green,
}

pub fn run() {}

impl Bar {}
";
        let unit = parse(src).expect("clean source");
        // Byte-identical emit holds regardless of structured recovery.
        assert_eq!(unit.emit(), src);

        let items: Vec<&ItemNode> = unit.items().collect();
        let kinds: Vec<&ItemKind> = items.iter().map(|i| &i.kind).collect();
        assert_eq!(
            kinds,
            vec![
                &ItemKind::Use,
                &ItemKind::Struct,
                &ItemKind::Enum,
                &ItemKind::Fn,
                &ItemKind::Impl,
            ]
        );

        let bar = items.iter().find(|i| i.name.as_deref() == Some("Bar")).unwrap();
        assert_eq!(bar.kind, ItemKind::Struct);
        assert_eq!(bar.visibility.as_deref(), Some("pub"));

        let run = items.iter().find(|i| i.name.as_deref() == Some("run")).unwrap();
        assert_eq!(run.kind, ItemKind::Fn);
        assert_eq!(run.visibility.as_deref(), Some("pub"));

        let color = items.iter().find(|i| i.name.as_deref() == Some("Color")).unwrap();
        assert_eq!(color.kind, ItemKind::Enum);
        assert_eq!(color.visibility, None);
    }

    #[test]
    fn structured_doc_and_attrs_are_recovered() {
        let src = "\
/// line one
/// line two
#[derive(Debug)]
#[allow(dead_code)]
pub struct Documented;
";
        let unit = parse(src).expect("clean source");
        let item = unit.items().next().unwrap();
        assert_eq!(item.name.as_deref(), Some("Documented"));
        assert_eq!(item.doc, vec!["/// line one", "/// line two"]);
        assert_eq!(item.attrs, vec!["#[derive(Debug)]", "#[allow(dead_code)]"]);
        // Lossless text still carries everything for emit.
        assert_eq!(unit.emit(), src);
    }

    #[test]
    fn surgical_edit_re_renders_only_the_edited_item() {
        let src = "\
pub struct A {
    x: u32,
}

pub struct B {
    y: u32,
}
";
        let mut unit = parse(src).expect("clean source");
        // Edit only struct A's field type.
        let idx = unit
            .segments
            .iter()
            .position(|s| matches!(s, Segment::Item(i) if i.name.as_deref() == Some("A")))
            .unwrap();
        if let Segment::Item(item) = &mut unit.segments[idx] {
            item.text = item.text.replace(": u32", ": u64");
        }
        let out = unit.emit();
        // A changed; the whole file still parses; B is untouched byte-for-byte.
        assert!(out.contains("x: u64"));
        assert!(out.contains("y: u32"));
        assert!(parse(&out).is_ok(), "edited unit must still be clean Rust");
        assert!(out.contains("pub struct B {\n    y: u32,\n}"));
    }
}

fn jet_js_array(value: &wasm_bindgen::JsValue, key: &str) -> js_sys::Array {
    let nested = jet_js_get(value, key);
    if js_sys::Array::is_array(&nested) {
        js_sys::Array::from(&nested)
    } else {
        js_sys::Array::new()
    }
}

fn jet_projects_array(projects: &Option<wasm_bindgen::JsValue>) -> js_sys::Array {
    projects
        .as_ref()
        .map(js_sys::Array::from)
        .unwrap_or_else(js_sys::Array::new)
}

fn jet_array_find_by_id(array: &js_sys::Array, id: &str) -> Option<wasm_bindgen::JsValue> {
    for idx in 0..array.length() {
        let item = array.get(idx);
        if jet_js_str(&item, "id") == id {
            return Some(item);
        }
    }
    None
}

fn jet_active_project(
    projects: &Option<wasm_bindgen::JsValue>,
    active_index: i64,
) -> Option<wasm_bindgen::JsValue> {
    let array = jet_projects_array(projects);
    if array.length() == 0 {
        return None;
    }
    let bounded = active_index.max(0).min((array.length() - 1) as i64) as u32;
    Some(array.get(bounded))
}


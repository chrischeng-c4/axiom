fn jet_post_init(body: String) -> wasm_bindgen::JsValue {
    let init = js_sys::Object::new();
    let _ = js_sys::Reflect::set(
        &init,
        &wasm_bindgen::JsValue::from_str("method"),
        &wasm_bindgen::JsValue::from_str("POST"),
    );
    let headers = js_sys::Object::new();
    let _ = js_sys::Reflect::set(
        &headers,
        &wasm_bindgen::JsValue::from_str("Content-Type"),
        &wasm_bindgen::JsValue::from_str("application/json"),
    );
    let _ = js_sys::Reflect::set(
        &init,
        &wasm_bindgen::JsValue::from_str("headers"),
        &headers,
    );
    let _ = js_sys::Reflect::set(
        &init,
        &wasm_bindgen::JsValue::from_str("body"),
        &wasm_bindgen::JsValue::from_str(&body),
    );
    init.into()
}


/// Emit the imports + boilerplate every generated file shares.
/// @spec .aw/tech-design/projects/jet/semantic/jet-tsx-to-rust.md#schema
pub fn prelude(out: &mut Emitter) {
    out.push_line("// GENERATED by jet::tsx_to_rust. Do not edit by hand.");
    out.push_line("#![allow(unused_imports, unused_variables, unused_parens)]");
    out.push_line("");
    // Generic WASM substrate — Element tree, Props bag, Callback,
    // Component wrapper. Framework-agnostic; Vue / Angular codegen
    // would use the same three lines.
    out.push_line("use jet_wasm::{Callback, Component, Element, Props};");
    // React-specific runtime — hooks + mount/flush commit loop.
    out.push_line("use jet_wasm::react::{mount, use_state};");
    out.push_line("use std::rc::Rc;");
    out.push_line("");
}

// ── Interface declaration → Props struct ────────────────────────────────────
//
// Extremely narrow: only accepts an interface that has
// `Prop1: type1; Prop2: type2; ...` body and names one of Rust's
// primitive types per field. Anything else (optional, unions,
// nested types) falls out of the subset.

/// @spec .aw/tech-design/projects/jet/semantic/jet-tsx-to-rust.md#schema
pub fn props_interface(out: &mut Emitter, node: Node, source: &str) -> Result<()> {
    let name_node = first_child_of_kind(node, "type_identifier")
        .ok_or_else(|| reject(node, "interface without name"))?;
    let name = node_text(name_node, source);
    let body = first_child_of_kind(node, "interface_body")
        .ok_or_else(|| reject(node, "interface without body"))?;

    out.push_line("#[derive(Clone, Debug)]");
    out.push_line(&format!("pub struct {name} {{"));
    out.indent();
    let mut walker = body.walk();
    for member in body.named_children(&mut walker) {
        match member.kind() {
            "property_signature" => {
                let field_name_node = first_child_of_kind(member, "property_identifier")
                    .ok_or_else(|| reject(member, "field without name"))?;
                let field_name = node_text(field_name_node, source);
                let type_annot = first_child_of_kind(member, "type_annotation")
                    .ok_or_else(|| reject(member, "field without type"))?;
                // type_annotation's first named child is the actual type.
                let mut t_walker = type_annot.walk();
                let ty_node = type_annot
                    .named_children(&mut t_walker)
                    .next()
                    .ok_or_else(|| reject(type_annot, "empty type annotation"))?;
                let rust_ty = ts_type_to_rust(ty_node, source)?;
                out.prop_field_types
                    .insert(format!("{name}.{field_name}"), rust_ty.clone());
                out.push_line(&format!("pub {field_name}: {rust_ty},"));
            }
            other => bail!(
                "interface member `{other}` outside the spike subset at {}",
                format_pos(member)
            ),
        }
    }
    out.dedent();
    out.push_line("}");
    out.push_line("");
    Ok(())
}

fn ts_type_to_rust(node: Node, source: &str) -> Result<String> {
    match node.kind() {
        "predefined_type" => match node_text(node, source) {
            "number" => Ok("i64".to_string()),
            "string" => Ok("String".to_string()),
            "boolean" => Ok("bool".to_string()),
            other => Err(reject(
                node,
                &format!("predefined type `{other}` not mapped"),
            )),
        },
        // `number[]` / `string[]` / etc. — tree-sitter-typescript emits
        // this as `array_type` whose first named child is the element
        // type. Always lowers to `Vec<T>` so the prop field is owned
        // and can be iterated with `.iter()` without lifetime gymnastics.
        "array_type" => {
            let inner = node
                .named_children(&mut node.walk())
                .next()
                .ok_or_else(|| reject(node, "array_type missing element type"))?;
            let inner_rust = ts_type_to_rust(inner, source)?;
            Ok(format!("Vec<{inner_rust}>"))
        }
        other => Err(reject(node, &format!("type kind `{other}` not supported"))),
    }
}

// ── Function component → render fn + Component factory ──────────────────────

/// @spec .aw/tech-design/projects/jet/semantic/jet-tsx-to-rust.md#schema
pub fn function_component(out: &mut Emitter, node: Node, source: &str) -> Result<()> {
    // Name — required.
    let name_node = first_child_of_kind(node, "identifier")
        .ok_or_else(|| reject(node, "component without name"))?;
    let name = node_text(name_node, source);

    // Record component position for the side-car map + annotate the
    // emitted render fn so a reader landing on it in DevTools sees
    // where it came from.
    let cp = name_node.start_position();
    out.position_map.components.push(ComponentPos {
        name: name.to_string(),
        tsx_line: cp.row + 1,
        tsx_col: cp.column + 1,
    });
    out.push_tsx_annot(node);

    // Formal parameters — spike requires exactly one, destructured, typed with
    // a named interface (emitted earlier).
    let params = first_child_of_kind(node, "formal_parameters")
        .ok_or_else(|| reject(node, "component without parameters"))?;
    let (field_names, props_type) = parse_destructured_props(params, source)?;

    // Body — a statement_block whose last statement is a return_statement.
    let body = first_child_of_kind(node, "statement_block")
        .ok_or_else(|| reject(node, "component without body"))?;

    let snake = to_snake(name);
    let render_fn = format!("{snake}_render");

    // Emit the render function.
    out.push_line(&format!(
        "fn {render_fn}(props: &Rc<dyn std::any::Any>) -> Element {{"
    ));
    out.indent();
    out.push_line(&format!(
        "let props: &{props_type} = props.downcast_ref().expect(\"{props_type}\");"
    ));
    // Destructure each prop field as a local `&Ty` so JSX
    // interpolations like `{value}` resolve without the transpiler
    // having to thread `prop_fields` through every emit_jsx_*
    // function. For components that read a field via `useState`
    // this is a redundant borrow that rustc elides — for hookless
    // components it's load-bearing.
    for f in &field_names {
        out.push_line(&format!("let {f} = &props.{f};"));
    }

    // Walk body statements in order. Only:
    // - variable_declaration with useState (→ hooks)
    // - return_statement (→ the JSX expression)
    let mut walker = body.walk();
    for stmt in body.named_children(&mut walker) {
        match stmt.kind() {
            "lexical_declaration" | "variable_declaration" => {
                emit_hook_binding(out, stmt, source, &field_names, &props_type)?;
            }
            "expression_statement" => {
                emit_effect_call(out, stmt, source)?;
            }
            "return_statement" => {
                let expr = stmt
                    .named_children(&mut stmt.walk())
                    .next()
                    .ok_or_else(|| reject(stmt, "empty return"))?;
                let rendered = emit_jsx_expr(expr, source)?;
                out.push_line(&format!("{rendered}"));
            }
            "empty_statement" => {}
            other => {
                bail!(
                    "statement `{other}` outside the spike subset at {}",
                    format_pos(stmt)
                )
            }
        }
    }
    out.dedent();
    out.push_line("}");
    out.push_line("");

    // Emit a Component factory. Arg types come from the prop-field
    // type map populated by `props_interface`; defaults to i64 if the
    // interface wasn't emitted earlier (should not happen inside the
    // subset, but keeps compilation moving).
    let factory_name = to_snake(name);
    let mut ctor_args = String::new();
    let mut ctor_inits = String::new();
    for (i, f) in field_names.iter().enumerate() {
        if i > 0 {
            ctor_args.push_str(", ");
            ctor_inits.push_str(", ");
        }
        let field_ty = out
            .prop_field_types
            .get(&format!("{props_type}.{f}"))
            .cloned()
            .unwrap_or_else(|| "i64".to_string());
        ctor_args.push_str(&format!("{f}: {field_ty}"));
        ctor_inits.push_str(&format!("{f}"));
    }
    out.push_line(&format!(
        "pub fn {factory_name}({ctor_args}) -> Component {{"
    ));
    out.indent();
    out.push_line("Component {");
    out.indent();
    out.push_line(&format!("name: \"{name}\","));
    out.push_line(&format!("render: {render_fn},"));
    out.push_line(&format!("props: Rc::new({props_type} {{ {ctor_inits} }}),"));
    out.dedent();
    out.push_line("}");
    out.dedent();
    out.push_line("}");
    out.push_line("");

    Ok(())
}

/// Emit a best-effort Rust/WASM component for compatibility builds.
///
/// This path intentionally keeps app logic out of JS. It lowers the
/// root JSX shell to Jet `Element` data, maps known external UI
/// components to intrinsic nodes, and drops unsupported dynamic
/// expressions as `Element::Empty`.
/// @spec .aw/tech-design/projects/jet/semantic/jet-tsx-to-rust.md#schema
pub fn compat_function_component(
    out: &mut Emitter,
    node: Node,
    source: &str,
    name: &str,
    aliases: &HashMap<String, String>,
) -> Result<()> {
    let name_node = first_child_of_kind(node, "identifier")
        .ok_or_else(|| reject(node, "component without name"))?;
    let cp = name_node.start_position();
    out.position_map.components.push(ComponentPos {
        name: name.to_string(),
        tsx_line: cp.row + 1,
        tsx_col: cp.column + 1,
    });
    out.push_tsx_annot(node);

    let body = first_child_of_kind(node, "statement_block")
        .ok_or_else(|| reject(node, "component without body"))?;
    let return_expr = find_top_level_return_expr(body)
        .ok_or_else(|| reject(body, "compat component must return JSX"))?;

    let snake = to_snake(name);
    let render_fn = format!("{snake}_render");
    out.push_line(&format!(
        "fn {render_fn}(_props: &Rc<dyn std::any::Any>) -> Element {{"
    ));
    out.indent();
    for stmt in body.named_children(&mut body.walk()) {
        if matches!(stmt.kind(), "lexical_declaration" | "variable_declaration") {
            emit_compat_local_binding(out, stmt, source)?;
        }
    }
    let rendered = compat_jsx_expr(return_expr, source, aliases)?;
    out.push_line(&rendered);
    out.dedent();
    out.push_line("}");
    out.push_line("");

    out.push_line(&format!("pub fn {snake}() -> Component {{"));
    out.indent();
    out.push_line("Component {");
    out.indent();
    out.push_line(&format!("name: \"{name}\","));
    out.push_line(&format!("render: {render_fn},"));
    out.push_line("props: Rc::new(()),");
    out.dedent();
    out.push_line("}");
    out.dedent();
    out.push_line("}");
    out.push_line("");
    Ok(())
}

fn emit_compat_local_binding(out: &mut Emitter, stmt: Node, source: &str) -> Result<()> {
    let Some(declarator) = first_child_of_kind(stmt, "variable_declarator") else {
        return Ok(());
    };
    let Some(rhs) = first_child_of_kind(declarator, "call_expression") else {
        return Ok(());
    };
    let Some(callee) = first_child_of_kind(rhs, "identifier") else {
        return Ok(());
    };
    if node_text(callee, source) == "useState" {
        emit_use_state_binding(out, stmt, source, &[], "")?;
    }
    Ok(())
}


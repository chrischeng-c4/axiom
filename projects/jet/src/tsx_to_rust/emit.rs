//! Emit Rust source for the subset of TSX the spike handles.
//!
//! Every transpile rule here is **intentionally narrow**. The spike's
//! value is a working end-to-end path for the Counter component, not
//! coverage of arbitrary TSX. Each rule is gated behind an AST-kind
//! match; unknown shapes bubble up as loud errors with file:line:col.

use anyhow::{anyhow, bail, Result};
use std::collections::HashMap;
use tree_sitter::Node;

use super::{first_child_of_kind, format_pos, node_text, reject};

// ── Output builder ──────────────────────────────────────────────────────────

/// @spec .aw/tech-design/projects/jet/semantic/jet-tsx-to-rust.md#schema
pub struct Emitter {
    buf: String,
    indent: usize,
    /// Prop interface field → Rust type. Populated by
    /// `props_interface` so downstream rules (`use_state` turbofish)
    /// can infer types. Key format: "InterfaceName.field".
    pub prop_field_types: std::collections::HashMap<String, String>,
    /// Source filename (logical — from the caller's `transpile_with_source`).
    /// Used in `// @tsx <file>:<line>:<col>` annotations so when a
    /// reader lands on a Rust line in DevTools they can see the TSX
    /// origin right above. Empty = no annotations emitted.
    pub source_file: String,
    /// Components + elements collected during emit for the side-car
    /// tsx-source-map.json. Lets `jet browser tsx` list where things
    /// were declared without parsing the TSX again.
    pub position_map: PositionMap,
}

/// Side-car map payload. Emitted to `dist/tsx-source-map.json` by
/// `wasm_build::build_with_profile` after transpile; consumed by
/// `jet browser tsx` and (future) runtime tooling. Kept intentionally
/// small — element-level positions will land when the CLI grows a
/// "click-to-jump-to-TSX" flow.
/// @spec .aw/tech-design/projects/jet/semantic/jet-tsx-to-rust.md#schema
#[derive(Default, serde::Serialize)]
pub struct PositionMap {
    pub source_file: String,
    pub components: Vec<ComponentPos>,
}

/// @spec .aw/tech-design/projects/jet/semantic/jet-tsx-to-rust.md#schema
#[derive(serde::Serialize)]
pub struct ComponentPos {
    pub name: String,
    pub tsx_line: usize,
    pub tsx_col: usize,
}

/// @spec .aw/tech-design/projects/jet/semantic/jet-tsx-to-rust.md#schema
impl Emitter {
    pub fn new() -> Self {
        Self {
            buf: String::new(),
            indent: 0,
            prop_field_types: std::collections::HashMap::new(),
            source_file: String::new(),
            position_map: PositionMap::default(),
        }
    }
    pub fn push_line(&mut self, line: &str) {
        for _ in 0..self.indent {
            self.buf.push_str("    ");
        }
        self.buf.push_str(line);
        self.buf.push('\n');
    }
    pub fn push_block(&mut self, block: &str) {
        for line in block.lines() {
            self.push_line(line);
        }
    }
    /// Push a `// @tsx <source>:<line>:<col>` annotation comment.
    /// No-op when `source_file` is empty so the line-count of the
    /// generated Rust stays stable for snapshot tests that don't opt
    /// into annotations.
    pub fn push_tsx_annot(&mut self, node: Node) {
        if self.source_file.is_empty() {
            return;
        }
        let p = node.start_position();
        // `// @tsx src/Counter.tsx:9:4`  (1-indexed line + col so it
        // matches how editors report positions).
        self.push_line(&format!(
            "// @tsx {}:{}:{}",
            self.source_file,
            p.row + 1,
            p.column + 1
        ));
    }
    pub fn indent(&mut self) {
        self.indent += 1;
    }
    pub fn dedent(&mut self) {
        self.indent -= 1;
    }
    pub fn finish(self) -> String {
        self.buf
    }
    pub fn take_position_map(&mut self) -> PositionMap {
        let out = std::mem::take(&mut self.position_map);
        PositionMap {
            source_file: self.source_file.clone(),
            ..out
        }
    }
}

/// @spec .aw/tech-design/projects/jet/semantic/jet-tsx-to-rust.md#schema
impl Default for Emitter {
    fn default() -> Self {
        Self::new()
    }
}


#!/usr/bin/env python3.12
"""Auto-measure the ① Type axis and write it back to the README.

Two complementary type walls, both auto-graded by running the mamba binary:

  ENFORCEMENT (`tests/cpython/type/**`, ~8936 fixtures): force-typing's headline
  contract — mamba MUST reject a wrong-typed program where CPython silently
  accepts it. A fixture is ENFORCED when mamba rejects (a compile-time type
  error — stderr/stdout mentions "TypeError"/"type error" — OR a runtime
  `typeerror:` line) and LEAKED when it prints `no_typeerror:`. This mirrors the
  Rust harness `run_type_strict` verdict (runner.rs). No CPython oracle needed:
  the fixture itself encodes the must-reject expectation.

  SOUNDNESS (`tests/cpython/behavior/core/<family>/`, the 104-fixture wall):
  the complementary axis — given a CORRECTLY-typed op, does mamba compute the
  right VALUE (no NaN-box float leaks)? Graded green when mamba exits 0 and
  stdout matches the CPython 3.12 oracle.

Usage:
    MAMBA_BIN=… python3.12 tools/type_enforce_matrix.py [--jobs N] [--write]
    --write   inject the "Type capability — measured" block into the README
              between the TYPE-CAPABILITY markers.
"""
import os, re, subprocess, sys, argparse, shlex
from pathlib import Path
from concurrent.futures import ThreadPoolExecutor

ROOT = Path(__file__).resolve().parents[4]          # projects/mamba
TYPE_DIR = ROOT / "tests/cpython/type"
SOUND_DIR = ROOT / "tests/cpython/behavior/core"
README = ROOT / "README.md"
NON_RUNTIME_STUB_TYPE_LIB_PREFIXES = ("_typeshed",)
SOUND_FAMILIES = [
    "float_return_inference", "builtin_numeric_inference",
    "comprehension_float_inference", "generator_float_inference",
    "container_float_roundtrip", "value_equality_inference",
    "mixed_numeric_inference",
]
BEGIN = "<!-- TYPE-CAPABILITY:BEGIN (generated by tests/harness/cpython/tools/type_enforce_matrix.py — do not edit by hand) -->"
END = "<!-- TYPE-CAPABILITY:END -->"

def _default_mamba() -> str:
    if env := os.environ.get("MAMBA_BIN"):
        return env
    debug = (ROOT / "../../target/debug/mamba").resolve()
    if debug.exists():
        return str(debug)
    release = (ROOT / "../../target/release/mamba").resolve()
    if release.exists():
        return str(release)
    return "mamba"


MAMBA = _default_mamba()


def _is_non_runtime_stub_type_fixture(path: Path) -> bool:
    try:
        rel = path.relative_to(TYPE_DIR).parts
    except ValueError:
        return False
    if len(rel) < 3 or rel[0] != "std-libs":
        return False
    lib = rel[1]
    return any(
        lib == prefix or lib.startswith(f"{prefix}_")
        for prefix in NON_RUNTIME_STUB_TYPE_LIB_PREFIXES
    )


def _run(argv, timeout):
    try:
        r = subprocess.run(argv, capture_output=True, text=True, timeout=timeout)
        return r.returncode, r.stdout, r.stderr
    except Exception:
        return None, "", ""


def _has_line(text, prefix):
    return any(ln.strip().startswith(prefix) for ln in text.splitlines())


def grade_enforcement(fx: str):
    """Return (bucket, lib, verdict) where verdict ∈ {enforced, leaked, error}."""
    rc, out, err = _run(["/bin/sh", "-c",
                         "ulimit -t 8 2>/dev/null; ulimit -c 0 2>/dev/null; "
                         f"exec {shlex.quote(MAMBA)} run {shlex.quote(fx)}"], 12)
    # bucket = the dir under type/ (core, std-libs, …); lib = its child.
    rel = Path(fx).relative_to(TYPE_DIR).parts
    bucket = rel[0] if len(rel) > 1 else "core"
    lib = rel[1] if len(rel) > 2 else bucket
    leaked = _has_line(out, "no_typeerror:")
    runtime_te = _has_line(out, "typeerror:")
    compile_te = (rc is not None and rc != 0 and
                  ("TypeError" in err or "type error" in err
                   or "TypeError" in out or "type error" in out))
    if leaked and not runtime_te:
        v = "leaked"
    elif (runtime_te and not leaked) or compile_te:
        v = "enforced"
    else:
        v = "error"          # crash / timeout / malformed — not gradable
    return (bucket, lib, v)


def grade_soundness(fx: str):
    orc, oout, _ = _run(["python3.12", fx], 10)
    if orc != 0:
        return None
    rc, out, _ = _run(["/bin/sh", "-c",
                       "ulimit -t 8 2>/dev/null; "
                       f"exec {shlex.quote(MAMBA)} run {shlex.quote(fx)}"], 12)
    return rc == 0 and out.strip() == oout.strip()


def main(argv=None):
    ap = argparse.ArgumentParser()
    ap.add_argument("--jobs", type=int, default=os.cpu_count() or 8)
    ap.add_argument("--write", action="store_true")
    ap.add_argument("--limit", type=int, default=0, help="cap fixtures (debug)")
    ap.add_argument("--list-leaked", metavar="FILE",
                    help="write leaked fixture paths (one per line) and print top leak clusters by lib")
    args = ap.parse_args(argv)

    type_fx = sorted(str(p) for p in TYPE_DIR.rglob("*.py")
                     if not _is_non_runtime_stub_type_fixture(p))
    if args.limit:
        type_fx = type_fx[:: max(1, len(type_fx) // args.limit)][: args.limit]
    print(f"grading {len(type_fx)} enforcement fixtures with {args.jobs} jobs…",
          file=sys.stderr)

    by_bucket: dict = {}
    leaked_by_lib: dict = {}
    leaked_paths: list = []
    enf = leak = err = 0
    with ThreadPoolExecutor(max_workers=args.jobs) as ex:
        for (bucket, lib, v), fx in zip(
                ex.map(grade_enforcement, type_fx, chunksize=16), type_fx):
            d = by_bucket.setdefault(bucket, {"enforced": 0, "leaked": 0, "error": 0})
            d[v] += 1
            enf += v == "enforced"; leak += v == "leaked"; err += v == "error"
            if v == "leaked":
                leaked_by_lib[lib] = leaked_by_lib.get(lib, 0) + 1
                leaked_paths.append(fx)

    if args.list_leaked:
        Path(args.list_leaked).write_text("\n".join(sorted(leaked_paths)) + "\n")
        print(f"\n[leaked → {args.list_leaked}] {len(leaked_paths)} fixtures; "
              f"top leak clusters by lib:", file=sys.stderr)
        for lib, n in sorted(leaked_by_lib.items(), key=lambda kv: -kv[1])[:25]:
            print(f"    {n:4d}  {lib}", file=sys.stderr)

    gradable = enf + leak
    rate = (100.0 * enf / gradable) if gradable else 0.0

    # Soundness wall.
    sound_fx = sorted(str(p) for fam in SOUND_FAMILIES
                      for p in (SOUND_DIR / fam).glob("*.py"))
    sg = sr = 0
    with ThreadPoolExecutor(max_workers=args.jobs) as ex:
        for ok in ex.map(grade_soundness, sound_fx, chunksize=8):
            if ok is None:
                continue
            sg += bool(ok); sr += not ok

    # ── Markdown report ──
    lines = [BEGIN,
             "",
             "### Type capability — measured",
             "",
             f"Auto-graded by `tools/type_enforce_matrix.py` (mamba `{Path(MAMBA).name}` "
             f"vs the fixture contract). Two complementary walls:",
             "",
             "| wall | what it proves | gradable | green | rate |",
             "|------|----------------|----------|-------|------|",
             f"| **① Enforcement** | mamba rejects wrong-typed code CPython accepts | {gradable} | {enf} | **{rate:.1f}%** |",
             f"| **① Soundness** | correctly-typed ops compute the right value (no float leaks) | {sg + sr} | {sg} | **{(100.0*sg/(sg+sr) if sg+sr else 0):.1f}%** |",
             "",
             f"Enforcement: {enf} enforced · {leak} leaked · {err} ungradable "
             f"(crash/timeout/malformed) of {len(type_fx)} fixtures.",
             "",
             "| enforcement bucket | enforced | leaked | rate |",
             "|--------------------|----------|--------|------|"]
    for bucket in sorted(by_bucket):
        d = by_bucket[bucket]
        g = d["enforced"] + d["leaked"]
        r = (100.0 * d["enforced"] / g) if g else 0.0
        lines.append(f"| {bucket} | {d['enforced']} | {d['leaked']} | {r:.1f}% |")
    lines += ["", END]
    block = "\n".join(lines)

    print(block)

    if args.write:
        text = README.read_text()
        if BEGIN in text and END in text:
            text = re.sub(re.escape(BEGIN) + r".*?" + re.escape(END), block,
                          text, flags=re.S)
        else:
            # Insert after the "## Capability status" heading block if present,
            # else append.
            anchor = "## Capability status"
            idx = text.find(anchor)
            if idx != -1:
                nxt = text.find("\n## ", idx + len(anchor))
                nxt = nxt if nxt != -1 else len(text)
                text = text[:nxt] + "\n" + block + "\n" + text[nxt:]
            else:
                text = text.rstrip() + "\n\n" + block + "\n"
        README.write_text(text)
        print(f"\n[written] {README}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

#!/usr/bin/env python3.12
"""Regenerable README "Library coverage" matrix.

The README matrix used to be hand-assembled. This regenerates it from the fixture
tree so the numbers move only when the runtime actually improves.

COLLAPSE: std-libs submodule rows fold into their top-level CPython module
(asyncio.tasks -> asyncio; every encodings_* codec page -> encodings), derived
from each fixture's [tool.mamba] `source`/`subject` metadata — CPython's OWN
module taxonomy, never ad-hoc name-splitting. The unit is the top-level
importable module, the least-inflatable denominator. Core-language and built-in
buckets are mamba FEATURE buckets (not Python modules) and are NOT collapsed.

PASS = a sampled fixture runs under `mamba run` and exits 0 with a trailing `OK`
self-check marker (<=N fixtures/lib, conservative floor — value-printing fixtures
that match CPython exactly but don't end in OK are undercounted). Aggregation is
fixture-weighted; tiers ✅>=80 / 🟢50-79 / 🟡1-49 / 🔴0.

Usage:
    python3.12 tools/coverage_matrix.py --no-run            # structure only (fast)
    python3.12 tools/coverage_matrix.py --sample 8          # run, print markdown
    python3.12 tools/coverage_matrix.py --sample 8 --write  # inject README + COVERAGE.md

    MAMBA_BIN env (or `which mamba`) selects the binary.
"""
from __future__ import annotations

import argparse
import os
import re
import shutil
import sys
from collections import defaultdict
from pathlib import Path

import harness_lib  # shared oracle/SUT runner (tools/): isolated scratch CWD + PYTHONBREAKPOINT=0
from wall_gen_core import parse_header  # shared PEP-723 [tool.mamba] header parse

TOOLS_DIR = Path(__file__).resolve().parent
FIXTURES_DIR = TOOLS_DIR.parents[2] / "cpython"          # projects/mamba/tests/cpython
MAMBA_ROOT = TOOLS_DIR.parents[3]                        # projects/mamba
README = MAMBA_ROOT / "README.md"
DRILLDOWN = MAMBA_ROOT / "COVERAGE.md"

BEGIN = "<!-- COVERAGE:BEGIN (generated by tests/harness/cpython/tools/coverage_matrix.py — do not edit by hand) -->"
END = "<!-- COVERAGE:END -->"

# bucket -> README section. Only these three buckets form the lib-coverage matrix;
# pep / security-matrix / perf are separate dimensions, not per-lib coverage rows.
SECTIONS = [
    ("core", "Core language features"),
    ("builtin-libs", "Built-in libraries"),
    ("std-libs", "Standard library"),
]
# Buckets whose `lib` is a top-level CPython module already / a mamba feature
# bucket — do NOT collapse (collapse only folds std-libs SUBMODULES).
COLLAPSE_BUCKETS = {"std-libs"}

def top_level_module(meta: dict) -> str:
    """Top-level CPython module for a std-libs fixture, from source/subject."""
    src = meta.get("source", "") or ""
    if "typeshed/stdlib/" in src:
        rest = src.split("typeshed/stdlib/", 1)[1]
        head = rest.split("/", 1)[0]
        return head[:-4] if head.endswith(".pyi") else head
    if "Lib/test/" in src:
        rest = src.split("Lib/test/", 1)[1]
        head = rest.split("/", 1)[0]               # test_asyncio/ OR test_x.py
        if head.endswith(".py"):
            head = head[:-3]
        if head.startswith("test_"):
            head = head[5:]
        return head or meta.get("lib", "?")
    subj = meta.get("subject", "") or ""
    if subj and subj != "":
        first = re.split(r"[.\(\[ ]", subj, 1)[0]
        # behavior subjects look like cpython.test_x.Class.method
        if first == "cpython":
            parts = subj.split(".")
            if len(parts) > 1:
                seg = parts[1]
                return seg[5:] if seg.startswith("test_") else seg
        if first:
            return first
    return meta.get("lib", "?")


def lib_key(meta: dict) -> str:
    bucket = meta.get("bucket", "")
    if bucket in COLLAPSE_BUCKETS:
        return top_level_module(meta)
    return meta.get("lib", "?")


def _spawn(argv, timeout):
    rc, out, _err = harness_lib.run_fixture(argv, timeout)
    return rc, out


def run_fixture(mamba_bin: str, fixture: Path, timeout: int):
    """② Behavior pass: mamba's stdout matches the live CPython 3.12 oracle.

    Returns True (match), False (diverge/crash), or None (oracle-skip: CPython
    itself errors on the fixture, so it is not a mamba failure and leaves the
    denominator). This is the README's ② definition (live oracle, no goldens)."""
    orc, oout = _spawn(["python3.12", str(fixture)], timeout)
    if orc != 0:
        return None  # CPython errored / timed out -> not gradable
    inner = f"ulimit -t {timeout} 2>/dev/null; ulimit -c 0 2>/dev/null; exec {mamba_bin} run {fixture}"
    mrc, mout = _spawn(["/bin/sh", "-c", inner], timeout + 5)
    return mrc == 0 and mout.strip() == oout.strip()


def tier(pct: float, passed: int) -> str:
    if passed == 0:
        return "🔴"
    if pct >= 80:
        return "✅"
    if pct >= 50:
        return "🟢"
    return "🟡"


def discover() -> dict:
    """section_title -> {lib -> {'fixtures': [Path], 'submods': set}}"""
    out: dict = {title: defaultdict(lambda: {"fixtures": [], "submods": set()})
                 for _, title in SECTIONS}
    bucket_to_title = {b: t for b, t in SECTIONS}
    # ② Behavior corpus only — "library coverage" = does the lib RUN correctly
    # vs the live CPython oracle. (Other dimensions are separate gates.)
    for fx in (FIXTURES_DIR / "behavior").rglob("*.py"):
        meta = parse_header(fx)
        title = bucket_to_title.get(meta.get("bucket", ""))
        if not title:
            continue
        key = lib_key(meta)
        rec = out[title][key]
        rec["fixtures"].append(fx)
        if meta.get("bucket") in COLLAPSE_BUCKETS:
            rec["submods"].add(meta.get("lib", key))
    # Second pass (std-libs only): CPython ships several test files per package
    # (test_multiprocessing_fork, test_asyncio's _asyncio accelerator, …). Fold a
    # key into a sibling whose name is its leading segment when that sibling is
    # itself a top-level module here — principled, no hand-curated list. Keys with
    # no such parent (e.g. codecencodings_cn) are left as-is.
    stdlib = out["Standard library"]
    keys = set(stdlib)
    for k in sorted(stdlib, key=len, reverse=True):
        if k not in stdlib:
            continue
        stem = k[1:] if k.startswith("_") else k
        parent = stem.split("_", 1)[0]
        if parent and parent != k and parent in keys and parent in stdlib:
            stdlib[parent]["fixtures"].extend(stdlib[k]["fixtures"])
            stdlib[parent]["submods"].update(stdlib[k]["submods"] or {k})
            del stdlib[k]
    return out


def sample(fixtures: list, n: int) -> list:
    fixtures = sorted(fixtures)
    if len(fixtures) <= n:
        return fixtures
    step = max(1, len(fixtures) // n)
    return fixtures[::step][:n]


def main(argv=None) -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--sample", type=int, default=8, help="fixtures sampled per lib")
    ap.add_argument("--timeout", type=int, default=10)
    ap.add_argument("--no-run", action="store_true", help="structure only, no mamba run")
    ap.add_argument("--write", action="store_true", help="inject README + write COVERAGE.md")
    args = ap.parse_args(argv)

    mamba_bin = os.environ.get("MAMBA_BIN") or shutil.which("mamba") or "mamba"
    if not args.no_run and not (Path(mamba_bin).exists() or shutil.which(mamba_bin)):
        print(f"mamba binary not found ({mamba_bin}); set MAMBA_BIN or use --no-run",
              file=sys.stderr)
        return 2

    tree = discover()
    matrix_md: list[str] = []
    drill_md: list[str] = ["# Library coverage — per-submodule drill-down\n",
                           "Generated by `tests/harness/cpython/tools/coverage_matrix.py`. "
                           "The README shows top-level modules; this expands the collapsed "
                           "std-libs submodules.\n"]
    grand = {"✅": 0, "🟢": 0, "🟡": 0, "🔴": 0}

    for _, title in SECTIONS:
        libs = tree[title]
        rows = []
        sub_rows = []
        for lib in sorted(libs):
            rec = libs[lib]
            fxs = rec["fixtures"]
            chosen = sample(fxs, args.sample)
            if args.no_run:
                t = "·"
                passed = 0
                cell = f"{len(fxs)} fx · {len(rec['submods']) or 1} sub"
            else:
                results = [run_fixture(mamba_bin, fx, args.timeout) for fx in chosen]
                graded = [r for r in results if r is not None]   # drop oracle-skips
                passed = sum(1 for r in graded if r)
                if not graded:
                    t = "·"           # CPython itself can't run any sample -> not gradable
                    cell = "0/0"
                else:
                    pct = 100 * passed / len(graded)
                    t = tier(pct, passed)
                    cell = f"{passed}/{len(graded)}"
                    grand[t] += 1
            rows.append((t, lib, cell, len(rec["submods"])))
            if rec["submods"] and len(rec["submods"]) > 1:
                sub_rows.append((lib, sorted(rec["submods"]), len(fxs)))
        # tier order for display
        order = {"✅": 0, "🟢": 1, "🟡": 2, "🔴": 3, "·": 4}
        rows.sort(key=lambda r: (order[r[0]], r[1]))
        counts = {k: sum(1 for r in rows if r[0] == k) for k in ("✅", "🟢", "🟡", "🔴")}
        matrix_md.append(f"\n### {title} — {len(rows)} libs")
        if not args.no_run:
            matrix_md.append(f"✅ {counts['✅']} · 🟢 {counts['🟢']} · 🟡 {counts['🟡']} · "
                             f"🔴 {counts['🔴']}  (✅≥80% · 🟢 50–79% · 🟡 1–49% · 🔴 0%, "
                             f"top-level module, sampled ≤{args.sample}/lib)")
        matrix_md.append("\n| lib | | pass | sub |")
        matrix_md.append("|-----|--|------|-----|")
        for t, lib, cell, nsub in rows:
            matrix_md.append(f"| `{lib}` | {t} | {cell} | {nsub} |")
        # drill-down
        if sub_rows:
            drill_md.append(f"\n## {title}\n")
            for lib, submods, nfx in sub_rows:
                drill_md.append(f"- **`{lib}`** ({nfx} fixtures, {len(submods)} submodules): "
                                + ", ".join(f"`{s}`" for s in submods))

    total_libs = sum(len(tree[t]) for _, t in SECTIONS)
    header = ["## Library coverage — what's implemented, what's stub", "",
              f"Per-**library** ② behavior status scoped to **CPython 3.12**. The unit is "
              f"the **top-level importable module** — std-libs submodules are folded into "
              f"their parent via each fixture's CPython `source`/`subject` metadata (all "
              f"`asyncio.*` → `asyncio`, every codec page → `encodings`), so the denominator "
              f"is CPython's own taxonomy, not an inflatable per-file split. Pass = mamba's "
              f"stdout matches the **live CPython 3.12 oracle** (≤{args.sample} sampled "
              f"fixtures/lib; fixtures CPython itself can't run are excluded, not graded). "
              f"Core-language and built-in rows are mamba feature buckets, not modules, and "
              f"are not collapsed. Regenerate: `MAMBA_BIN=… tools/coverage_matrix.py "
              f"--sample 8 --write`; per-submodule detail in [COVERAGE.md](COVERAGE.md)."]
    if not args.no_run:
        header.append("")
        header.append(f"**Overall: ✅ {grand['✅']} · 🟢 {grand['🟢']} · 🟡 {grand['🟡']} · "
                      f"🔴 {grand['🔴']}** across {total_libs} top-level libs.")
    body = "\n".join(header) + "\n" + "\n".join(matrix_md) + "\n"

    # The drill-down is static structure (no run data), so always refresh it.
    DRILLDOWN.write_text("\n".join(drill_md) + "\n")

    if args.write:
        text = README.read_text()
        if BEGIN not in text or END not in text:
            print(f"README markers not found; add the BEGIN/END markers around the "
                  f"coverage section first.", file=sys.stderr)
            return 3
        pre = text[: text.index(BEGIN) + len(BEGIN)]
        post = text[text.index(END):]
        README.write_text(pre + "\n" + body + "\n" + post)
        print(f"injected into {README} and wrote {DRILLDOWN}")
    else:
        print(body)
        print(f"\n[wrote drill-down: {DRILLDOWN}]", file=sys.stderr)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

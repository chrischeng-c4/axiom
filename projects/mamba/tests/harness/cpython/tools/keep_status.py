#!/usr/bin/env python3.12
"""Phase 2 KEEP meter — per-dimension mamba-vs-CPython pass-rate.

Phase 1 built the walls (coverage 100%); this measures the SUBJECT: how many wall
fixtures mamba actually passes. Reuses gate_status's sandboxed run, but classifies
per each axis's REAL pass definition (gate_status's plain stdout-match is wrong
for Type, which is inverse, and for Perf, which is CPU+RSS not stdout):

  (1) Type     PASS = mamba ENFORCED the contract. mamba force-types at COMPILE
               time, so enforcement has two forms: a runtime "typeerror:" marker
               (mrc==0), OR a non-zero exit whose diagnostic is a genuine
               value-vs-annotation type mismatch (a "type mismatch" /
               operand/operator domain rejection, on stderr or stdout). Every
               type-checker diagnostic shares the "type error at <span>: …"
               envelope, so matching the bare "type error" prefix would mis-score
               undefined-name / unknown-type reds as enforcement passes; we match
               the mismatch message body and veto the resolution-class messages.
               "no_typeerror:"/"accepted" is DIVERGE (type not enforced); a
               non-zero exit that is NOT a real type mismatch (panic / syntax
               error / undefined name / unknown type) is a genuine MAMBA_RED.
  (2) Behavior PASS = mamba exit 0 AND stdout == CPython oracle stdout.
  (3) Perf     PASS = mamba runs (exit 0). The real CPU+RSS-beats-CPython gate is
               perf_pin (separate, MAMBA_REQUIRE_CPYTHON_PERF_BASELINE); here a
               run is RUNNABLE, a crash is MAMBA_RED — printed as a sub-metric.
  (4) Safety   PASS = mamba prints "safe:" (message leaks no secret of that class)
               AND no crash/hang. "LEAK:" is DIVERGE; crash/timeout is MAMBA_RED.

DONE (D1): (1) 100% enforced · (2) 100% match · (3) 100% runnable+faster · (4) 0 leak/crash.

    python3.12 tools/keep_status.py --per-facet 40   # stratified sample
    python3.12 tools/keep_status.py                   # every fixture (slow)
"""

from __future__ import annotations

import argparse
import os
import shutil
from collections import Counter
from pathlib import Path

import harness_lib  # shared oracle/SUT runner (tools/): isolated scratch CWD + PYTHONBREAKPOINT=0
from wall_gen_core import parse_header  # shared PEP-723 [tool.mamba] header parse

TOOLS_DIR = Path(__file__).resolve().parent
FIXTURES_DIR = TOOLS_DIR.parents[2] / "cpython"
REPO_ROOT = TOOLS_DIR.parents[5]          # /Users/.../project-mamba
TYPE_DIVERGENCES = TOOLS_DIR.parent / "config" / "type_divergences.txt"
# Frozen-oracle gate config lists (generated by tools/gen_goldens.py; one repo-
# relative fixture path per line, '#' comments allowed):
#   nondeterministic -> --oracle golden falls back to a live python3.12 spawn.
#   oracle_skip      -> CPython itself errors; --oracle golden returns ORACLE_SKIP
#                       (distinguishes a skip from a "<case> OK" self-checker, which
#                       both leave no sidecar).
NONDET_GOLDENS = TOOLS_DIR / "config" / "nondeterministic_goldens.txt"
ORACLE_SKIP_GOLDENS = TOOLS_DIR / "config" / "oracle_skip_goldens.txt"


def _load_type_divergences() -> set[str]:
    """② denominator exclusions: behavior/PEP fixtures that are red *because* mamba
    correctly type-rejects what CPython silently accepts (force typing). Each entry
    is a repo-relative fixture path (same format/discipline as behavior_gaps.txt's
    CPython-fail exclusions); '#' comments allowed. These leave the ② passable
    denominator entirely — they are declared divergences, not mamba failures."""
    if not TYPE_DIVERGENCES.exists():
        return set()
    out: set[str] = set()
    for ln in TYPE_DIVERGENCES.read_text(encoding="utf-8").splitlines():
        ln = ln.strip()
        if not ln or ln.startswith("#"):
            continue
        out.add(ln)
    return out


def _repo_rel(fixture: Path) -> str:
    try:
        return str(fixture.resolve().relative_to(REPO_ROOT))
    except ValueError:
        return str(fixture)


def _load_path_list(path: Path) -> set[str]:
    """Load a gen_goldens config list: one repo-relative fixture path per line,
    '#' comments allowed (same format as type_divergences.txt)."""
    if not path.exists():
        return set()
    out: set[str] = set()
    for ln in path.read_text(encoding="utf-8").splitlines():
        ln = ln.strip()
        if not ln or ln.startswith("#"):
            continue
        out.add(ln)
    return out


def _parse_case(fixture: Path) -> str | None:
    """Extract `case` from the fixture's PEP-723 [tool.mamba] header (same parse as
    gen_goldens.py). Used to DERIVE the '<case> OK\\n' self-checker golden marker."""
    return parse_header(fixture).get("case")


# Loaded once in main(); consulted by classify() in --oracle golden mode.
_NONDET_GOLDENS: set[str] = set()
_ORACLE_SKIP_GOLDENS: set[str] = set()


def golden_oracle(fixture: Path, timeout: int) -> tuple[int | None, str]:
    """Frozen-oracle source of the CPython expected (orc, oout) for golden mode.

    Resolution order (mirrors tools/gen_goldens.py's classification; every fixture
    maps to exactly one branch):
      1. fixture listed oracle-skip      -> CPython errors; return (1, "") so
                                            classify() yields ORACLE_SKIP.
      2. <stem>.expected sidecar exists  -> oout = its exact bytes (decoded), orc=0.
      3. fixture listed non-deterministic -> live python3.12 spawn (unchanged).
      4. otherwise (the common self-checker) -> oout = '<case> OK\\n', orc=0.

    Only the SOURCE of (orc, oout) changes vs. live mode; the bytes handed to
    classify() are identical to what a clean python3.12 run produced when frozen."""
    rel = _repo_rel(fixture)
    if rel in _ORACLE_SKIP_GOLDENS:
        # CPython itself errored when frozen; non-zero rc -> classify() ORACLE_SKIP.
        return 1, ""
    sidecar = fixture.with_suffix(".expected")
    if sidecar.exists():
        # Frozen exact bytes. Decode as utf-8 to match `run()`'s text-mode stdout
        # (mamba stdout `mout` is likewise text-mode); the freeze preserved the
        # trailing newline, so this round-trips the live-mode comparison exactly.
        return 0, sidecar.read_bytes().decode("utf-8")
    if rel in _NONDET_GOLDENS:
        orc, oout, _oerr = run(["python3.12", str(fixture)], timeout)
        return orc, oout
    case = _parse_case(fixture)
    # Self-checker marker derived from the header. gen_goldens only takes this path
    # for fixtures it verified print EXACTLY '<case> OK\n', so this is the frozen
    # expected for them; oracle rc is treated as 0 (CPython ran clean when frozen).
    return 0, f"{case} OK\n"

# facet dir -> axis. Several behavior-family facets roll up into (2).
AXIS = {
    "type": "① Type",
    "behavior": "② Behavior", "surface": "② Behavior",
    "errors": "② Behavior", "real_world": "② Behavior",
    "perf": "③ Perf",
    "security": "④ Safety", "security-matrix": "④ Safety",
}
AXIS_ORDER = ["① Type", "② Behavior", "③ Perf", "④ Safety"]


def run(argv, timeout):
    # Isolated scratch CWD + PYTHONBREAKPOINT=0 via the shared runner so
    # test.support TESTFN side-effects never leak into the repo. 3-tuple: stderr
    # feeds the Type-facet compile-rejection check.
    return harness_lib.run_fixture(argv, timeout)


# mamba enforces types at COMPILE time: a wrong-typed annotated construct
# (`x: int = "3"`, `def f(c: int = "3")`, `list[int] = [1, "two"]`) exits non-zero
# with a genuine value-vs-annotation diagnostic instead of running the fixture's
# runtime try/except. That is enforcement, not a crash — distinct from a panic /
# syntax error / undefined name. We recognise the diagnostic on EITHER stream.
#
# CAUTION: every type-checker diagnostic is emitted as `MambaError::Type`, whose
# Display is `"type error at <span>: <message>"` (see src/error.rs). So matching
# the bare prefix `"type error"` (the old behaviour) also matches
# `"type error at L:C: undefined name: \`X\`"` and
# `"type error at L:C: unknown type: \`T\`"` — i.e. it mis-scored a fixture that
# is red because of an undefined name / unknown type as a Type-enforcement PASS,
# masking real regressions. We instead match ONLY the message bodies that mamba
# emits for a real value-vs-annotation rejection, and explicitly veto the
# resolution-class messages that ride the same `type error at …` envelope.
#
# Positive markers (genuine value-vs-annotation enforcement, from src/types/*.rs):
#   "type mismatch[: …]" / "… type mismatch[: …]"  (assignment, return, argument,
#                                                    operand)
#   the operator-domain rejections (`<op> requires numeric type` / `… int types`)
#   a real runtime `TypeError:` traceback (the mrc==0 marker path handles the
#   common case, but a TypeError that escapes uncaught surfaces here too).
TYPE_REJECTION_MARKERS = (
    "type mismatch",            # type/return/argument/operand/assignment mismatch
    "requires numeric type",    # arithmetic / unary +/- on non-numeric
    "requires numeric types",   # arithmetic/comparison operand domain
    "require int types",        # shift operators
    "TypeError:",               # uncaught runtime TypeError traceback
)
# Diagnostics that ride the same `type error at …` envelope but are NOT
# type-enforcement: a fixture red for any of these is a genuine MAMBA_RED, never a
# Type PASS. These are resolution / name failures, not value-vs-annotation checks.
NON_TYPE_REJECTION_MARKERS = (
    "undefined name",
    "unknown type",
    "unknown generic type",
)


def _is_type_rejection(stdout: str, stderr: str) -> bool:
    """True iff mamba rejected the fixture for a genuine value-vs-annotation type
    mismatch — NOT for an undefined name / unknown type / panic / syntax error.

    Scoring is deliberately strict: only real type-enforcement counts as the
    intended Type PASS, so a fixture that goes red for any other reason surfaces
    as MAMBA_RED instead of inflating the Type pass-rate."""
    blob = f"{stderr}\n{stdout}"
    if any(d in blob for d in NON_TYPE_REJECTION_MARKERS):
        return False
    return any(m in blob for m in TYPE_REJECTION_MARKERS)


def classify(fixture: Path, facet: str, mamba_bin: str, timeout: int,
             oracle: str = "golden") -> str:
    # SOURCE of the CPython expected (orc, oout) — golden (frozen) or live (spawn).
    # This is the ONLY behavioral switch; every facet's PASS/RED/DIVERGE logic below
    # is byte-for-byte identical regardless of where (orc, oout) came from.
    if oracle == "golden":
        orc, oout = golden_oracle(fixture, timeout)
    else:
        orc, oout, _oerr = run(["python3.12", str(fixture)], timeout)
    if orc != 0:
        return harness_lib.ORACLE_SKIP
    inner = f"ulimit -t {timeout} 2>/dev/null; ulimit -c 0 2>/dev/null; exec {mamba_bin} run {fixture}"
    mrc, mout, merr = run(["/bin/sh", "-c", inner], timeout + 5)
    if mrc is None:
        return harness_lib.MAMBA_RED
    if facet == "type":
        if mrc != 0:
            # mamba's enforcement FORM is a compile-time rejection: a non-zero exit
            # whose diagnostic is a TYPE error counts as enforced = PASS. Only a
            # non-type non-zero exit (panic / syntax error / undefined name) is a
            # genuine MAMBA_RED.
            return harness_lib.PASS if _is_type_rejection(mout, merr) else harness_lib.MAMBA_RED
        # mrc == 0: the fixture ran its runtime try/except and printed one marker.
        # Check the ACCEPT marker FIRST: "typeerror:" is a substring of
        # "no_typeerror:", so a naive `"typeerror:" in mout` mis-scores every
        # DIVERGE (accept) case as PASS and inflates the Type pass-rate.
        if "no_typeerror:" in mout:
            return harness_lib.DIVERGE
        # enforced iff mamba raised TypeError (printed the typeerror: marker)
        return harness_lib.PASS if "typeerror:" in mout else harness_lib.DIVERGE
    if facet == "perf":
        # runnable gate here; CPU+RSS-beats-CPython is perf_pin's job
        return harness_lib.MAMBA_RED if mrc != 0 else harness_lib.PASS
    if facet in ("security", "security-matrix"):
        if mrc != 0:
            return harness_lib.MAMBA_RED
        # leak-free iff no LEAK: marker and stdout matches the oracle's safe verdict
        if "LEAK:" in mout:
            return harness_lib.DIVERGE
        return harness_lib.PASS if mout == oout else harness_lib.DIVERGE
    # behavior / surface / errors / real_world: exact stdout match
    if mrc != 0:
        return harness_lib.MAMBA_RED
    return harness_lib.PASS if mout == oout else harness_lib.DIVERGE


def main(argv=None) -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--per-facet", type=int, default=None,
                    help="stratified sample: at most N spread across each facet")
    ap.add_argument("--timeout", type=int, default=10)
    ap.add_argument("--oracle", choices=("golden", "live"), default="live",
                    help="source of CPython expected output: live (spawn "
                         "python3.12, default) or golden (frozen committed "
                         "goldens). golden is only valid where gen_goldens has "
                         "frozen the corpus (errors + behavior sample so far); "
                         "default stays live until the full corpus is frozen.")
    args = ap.parse_args(argv)

    # Frozen-oracle gate: in golden mode, non-deterministic fixtures fall back to a
    # live spawn and oracle-skip fixtures return ORACLE_SKIP; loaded once and
    # consulted by golden_oracle() via classify().
    global _NONDET_GOLDENS, _ORACLE_SKIP_GOLDENS
    _NONDET_GOLDENS = _load_path_list(NONDET_GOLDENS)
    _ORACLE_SKIP_GOLDENS = _load_path_list(ORACLE_SKIP_GOLDENS)

    mamba_bin = os.environ.get("MAMBA_BIN") or shutil.which("mamba") or "mamba"
    per_axis: dict[str, Counter] = {a: Counter() for a in AXIS_ORDER}

    # ② denominator exclusions: declared type-divergences (mamba correctly
    # type-rejects what CPython accepts). These leave the ② passable denominator
    # entirely — never counted as PASS/RED/DIVERGE — same as behavior_gaps.txt.
    divergences = _load_type_divergences()
    excluded_count = 0

    print(f"{'facet':16} {'n':>6} {'PASS':>6} {'RED':>6} {'DIVERGE':>8} {'SKIP':>6}  pass_rate")
    for facet, axis in AXIS.items():
        root = FIXTURES_DIR / facet
        if not root.exists():
            continue
        fixtures = sorted(root.rglob("*.py"))
        # Drop declared type-divergences from the ② behavior-family denominator
        # (they are intentional force-typed rejections, not mamba failures).
        if axis == "② Behavior" and divergences:
            kept = [fx for fx in fixtures if _repo_rel(fx) not in divergences]
            excluded_count += len(fixtures) - len(kept)
            fixtures = kept
        if args.per_facet and len(fixtures) > args.per_facet:
            step = max(1, len(fixtures) // args.per_facet)
            fixtures = fixtures[::step][: args.per_facet]
        c: Counter = Counter()
        for fx in fixtures:
            c[classify(fx, facet, mamba_bin, args.timeout, args.oracle)] += 1
        per_axis[axis] += c
        _, graded, pr = harness_lib.compute_pass_rate(c)
        print(f"{facet:16} {sum(c.values()):6} {c[harness_lib.PASS]:6} {c[harness_lib.MAMBA_RED]:6} "
              f"{c[harness_lib.DIVERGE]:8} {c[harness_lib.ORACLE_SKIP]:6}  {pr:5.1f}%")

    if divergences:
        print(f"[excluded {excluded_count} declared type-divergences]")

    print(f"\n{'AXIS pass-rate':16}")
    lows = []
    for axis in AXIS_ORDER:
        c = per_axis[axis]
        _, graded, pr = harness_lib.compute_pass_rate(c)
        lows.append((pr, axis))
        print(f"  {axis:14} {pr:5.1f}%  ({c[harness_lib.PASS]}/{graded} graded · {c[harness_lib.MAMBA_RED]} RED · {c[harness_lib.DIVERGE]} wrong)")
    low_pr, low_axis = min(lows)
    print(f"\nLOWEST = {low_axis} at {low_pr:.1f}% — fix this dimension's runtime next.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

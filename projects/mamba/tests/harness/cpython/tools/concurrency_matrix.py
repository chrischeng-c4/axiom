#!/usr/bin/env python3.12
"""Measure the ⑦ Concurrency axis: a {shared-state op-pattern} × {interpreter}
thread-safety matrix, and write it back to the README.

Unlike every other conformance axis, concurrency CANNOT use "match CPython" as
its oracle:
  * CPython-WITH-GIL appears thread-safe for almost everything — but that is a
    GIL ARTIFACT (bytecode-boundary atomicity), not a language guarantee.
  * CPython free-threaded (PEP 703, 3.13t+) removes the GIL and exposes the real
    contract: a SINGLE built-in container mutation (`list.append`, `set.add`,
    `dict[k]=v`) is atomic (an internal per-object critical section), but a
    COMPOUND op (`c[0]+=1`, check-then-act) is NOT — it races and loses updates.

So mamba's concurrency CONTRACT is defined as free-threaded CPython 3.13t, not
GIL-CPython:
  * ATOMIC (must not corrupt or lose updates): a single built-in container
    mutation. Implemented with a lightweight per-object critical section taken
    ONLY on the mutation path, with a biased/uncontended fast path — NOT a lock
    around whole compound expressions (that would be both over-strict vs CPython
    and the expensive locking we explicitly reject).
  * NOT ATOMIC (races allowed; the caller must lock): compound ops (`+=`,
    check-then-act). Matching CPython means NOT over-promising here.
  * ABSOLUTE (never CORRUPT): no crash, no memory corruption, no impossible
    value — regardless of pattern. This is the Rust `Send`/`Sync` guarantee that
    replaces the GIL.

This tool runs each pattern under N unsynchronized threads × K ops, many trials,
on each available interpreter, and classifies the observed final-state
distribution: SAFE (always the expected value) · RACY (lost updates, no crash) ·
CORRUPT (crash / impossible value / wrong-but-deterministic = data corruption).

    python3.12 tools/concurrency_matrix.py            # print the matrix
    python3.12 tools/concurrency_matrix.py --write     # inject into README

A free-threaded CPython (`python3.13t`) is auto-discovered (PATH, then a uv
install). If absent the free-threaded column is omitted with a note — install it
with `uv python install cpython-3.13+freethreaded-<platform>`.
"""

from __future__ import annotations

import argparse
import os
import shutil
import subprocess
import sys
import tempfile
from pathlib import Path

TOOLS_DIR = Path(__file__).resolve().parent
MAMBA_DIR = TOOLS_DIR.parents[3]  # tools → cpython → harness → tests → mamba
README = MAMBA_DIR / "README.md"
# The cargo workspace target lives at the repo root (projects/mamba/../../target),
# matching tools/type_enforce_matrix.py. Override with MAMBA_BIN.
MAMBA_BIN = os.environ.get("MAMBA_BIN", str(MAMBA_DIR / ".." / ".." / "target" / "release" / "mamba"))

N_THREADS = 8
K_OPS = 5000
TRIALS = 20
EXPECT = N_THREADS * K_OPS

BEGIN = "<!-- CONCURRENCY-CAPABILITY:BEGIN (generated by tests/harness/cpython/tools/concurrency_matrix.py — do not edit by hand) -->"
END = "<!-- CONCURRENCY-CAPABILITY:END -->"

# Each pattern: (setup, per-iteration op using `tid` thread-index and `i`, observed final value).
# `tid` is the explicit thread index (0..N-1) — NOT threading.get_ident(), so the
# matrix is not confounded by get_ident behaviour differences across interpreters.
PATTERNS = {
    "int `+=` (shared, compound)": ("c = [0]", "c[0] += 1", "c[0]"),
    "list.append (shared)": ("L = []", "L.append(tid)", "len(L)"),
    "dict[k] `+=` (shared key, compound)": ("d = {'k': 0}", "d['k'] += 1", "d['k']"),
    "set.add (distinct keys)": ("s = set()", "s.add((tid, i))", "len(s)"),
    "check-then-act (per key)": ("d = {}", "d[i] = d.get(i, 0) + 1", "sum(d.values())"),
}


def discover_free_threaded() -> str | None:
    """Find a free-threaded CPython (PEP 703). PATH first, then uv's store."""
    for name in ("python3.13t", "python3.14t", "python3.15t"):
        p = shutil.which(name)
        if p and _is_free_threaded(p):
            return p
    uv = shutil.which("uv")
    if uv:
        store = Path.home() / ".local" / "share" / "uv" / "python"
        if store.is_dir():
            for d in sorted(store.glob("*freethreaded*"), reverse=True):
                cand = next(d.glob("bin/python3.1*t"), None)
                if cand and _is_free_threaded(str(cand)):
                    return str(cand)
    return None


def _is_free_threaded(python: str) -> bool:
    r = subprocess.run(
        [python, "-c", "import sys; print(not sys._is_gil_enabled())"],
        capture_output=True, text=True,
    )
    return r.stdout.strip() == "True"


def make_source(setup: str, op: str, observe: str) -> str:
    return (
        "import threading\n"
        f"{setup}\n"
        "def w(tid):\n"
        f"    for i in range({K_OPS}):\n"
        f"        {op}\n"
        f"ts = [threading.Thread(target=w, args=(t,)) for t in range({N_THREADS})]\n"
        "for t in ts: t.start()\n"
        "for t in ts: t.join()\n"
        f"print('R', {observe})\n"
    )


RUN_TIMEOUT = 30  # per-run cap; a slow runtime (e.g. mamba's O(n²) set) is a
# separate perf concern, not a thread-safety signal — report it, don't hang.


def run_once(cmd: list[str], source: str):
    with tempfile.NamedTemporaryFile("w", suffix=".py", delete=False) as f:
        f.write(source)
        path = f.name
    try:
        r = subprocess.run(cmd + [path], capture_output=True, text=True, timeout=RUN_TIMEOUT)
        for line in (r.stdout + r.stderr).splitlines():
            if line.startswith("R "):
                return ("ok", int(line.split()[1]))
        return ("err", (r.stdout + r.stderr).strip()[-70:])
    except subprocess.TimeoutExpired:
        return ("timeout", f">{RUN_TIMEOUT}s")
    except Exception as e:  # pragma: no cover - defensive
        return ("err", str(e)[:70])
    finally:
        os.unlink(path)


def run_trials(cmd: list[str], source: str, trials: int):
    """Run up to `trials` times, but stop at the first non-ok result — a timeout
    or error repeats identically, so 20× a 30s timeout is pure waste."""
    out = []
    for _ in range(trials):
        r = run_once(cmd, source)
        out.append(r)
        if r[0] != "ok":
            break
    return out


def classify(results, expect: int) -> str:
    vals = [v for st, v in results if st == "ok"]
    errs = [d for st, d in results if st != "ok"]
    timed_out = any(st == "timeout" for st, _ in results)
    if not vals:
        if timed_out:
            return f"ERR timeout ({results[0][1]})"
        return f"ERR ({errs[0] if errs else '?'})"
    lo, hi = min(vals), max(vals)
    suffix = f" +{len(errs)} crash" if errs else ""
    if lo == hi == expect:
        return f"SAFE {expect}{suffix}"
    if hi > expect:
        return f"CORRUPT >expect ({lo}..{hi}){suffix}"
    if lo == hi and lo < expect:
        return f"CORRUPT wrong-det {lo}/{expect}{suffix}"
    if hi < expect:
        return f"RACY {lo}..{hi}/{expect}{suffix}"
    return f"FLAKY {lo}..{hi}/{expect}{suffix}"


def build_matrix() -> str:
    interps: dict[str, list[str]] = {"CPython3.12 (GIL)": ["python3.12"]}
    ft = discover_free_threaded()
    if ft:
        interps["CPython3.13t (free-threaded)"] = [ft]
    interps["mamba"] = [MAMBA_BIN, "run"]

    lines = [BEGIN, "", "### Concurrency capability — measured", ""]
    lines.append(
        f"Thread-safety matrix auto-graded by `tools/concurrency_matrix.py` "
        f"(N={N_THREADS} unsynchronized threads × K={K_OPS} ops, {TRIALS} trials, "
        f"expected={EXPECT}). Oracle is **free-threaded CPython (PEP 703)**, NOT "
        f"GIL-CPython — the GIL masks races that the real contract exposes."
    )
    lines.append("")
    if not ft:
        lines.append(
            "> ⚠️ free-threaded CPython not found — column omitted. Install with "
            "`uv python install cpython-3.13+freethreaded-<platform>`."
        )
        lines.append("")
    lines.append("| shared-state pattern | " + " | ".join(interps) + " |")
    lines.append("|" + "---|" * (len(interps) + 1))
    for name, (setup, op, observe) in PATTERNS.items():
        src = make_source(setup, op, observe)
        cells = [
            classify(run_trials(cmd, src, TRIALS), EXPECT)
            for cmd in interps.values()
        ]
        lines.append(f"| {name} | " + " | ".join(cells) + " |")
    lines.append("")
    lines.append(
        "**Contract** (= free-threaded CPython 3.13t): a *single* built-in container "
        "mutation (`list.append`, `set.add`, `dict[k]=v`) is **atomic** (per-object "
        "critical section, uncontended-fast); a *compound* op (`+=`, check-then-act) "
        "is **not** atomic and the caller must lock; and **no** pattern may ever "
        "CORRUPT (crash / impossible value / wrong-deterministic result). "
        "GIL-CPython showing SAFE everywhere is the artifact the contract rejects."
    )
    lines.append("")
    lines.append(
        "An `ERR timeout` cell is a runtime too slow to finish this scale — a "
        "*performance* issue, not a thread-safety signal; thread-safety correctness "
        "at reduced scale is covered by the `concurrency/` fixtures."
    )
    lines.append("")
    lines.append(END)
    return "\n".join(lines)


def main(argv=None) -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--write", action="store_true", help="inject the block into the README")
    args = ap.parse_args(argv)

    if not Path(MAMBA_BIN).exists():
        print(f"!! mamba binary not found at {MAMBA_BIN} (build --release or set MAMBA_BIN)", file=sys.stderr)
        return 2

    block = build_matrix()
    print(block)

    if args.write:
        text = README.read_text()
        if BEGIN in text and END in text:
            pre = text[: text.index(BEGIN)]
            post = text[text.index(END) + len(END):]
            text = pre + block + post
        else:
            text = text.rstrip() + "\n\n" + block + "\n"
        README.write_text(text)
        print(f"\n[written] {README}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())


# List comparison and membership
print([1, 2, 3] == [1, 2, 3])
print([1, 2, 3] == [1, 2, 4])
print([1, 2] < [1, 3])
print([1, 2, 3] < [1, 2, 3])
print([1, 2, 3] < [1, 2, 3, 4])
print([1, 2, 3] > [1, 2])
print([1, 2, 3] <= [1, 2, 3])
print([1, 2, 3] >= [1, 2, 3])
print([1, 2, 3] != [1, 2, 4])
# Membership
print(2 in [1, 2, 3])
print(4 in [1, 2, 3])
print(4 not in [1, 2, 3])
# len
print(len([1, 2, 3]))
print(len([]))

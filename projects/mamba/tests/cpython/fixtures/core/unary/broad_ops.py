# unary operators

# arithmetic negation
print(-5)
print(-(-5))
print(-0)
print(-3.14)

# positive (unary +)
print(+5)
print(+0)
print(+(-5))

# bitwise not
print(~0)
print(~1)
print(~5)
print(~-1)

# boolean not
print(not True)
print(not False)
print(not 0)
print(not 1)
print(not "")
print(not "x")
print(not [])
print(not [1])
print(not None)

# unary with expressions
print(-(3 + 4))
print(-(3 * 4))
print(~(5 + 3))
print(not (3 < 5))

# double negation / not
print(not not True)
print(not not False)
print(not not 1)
print(not not [])

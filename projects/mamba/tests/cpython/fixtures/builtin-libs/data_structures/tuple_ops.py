
# Tuple operations
t = (1, 2, 3)
print(t)
print(t[0])
print(t[-1])
print(len(t))
# Comparison (lexicographic)
print((1, 2) < (1, 3))
print((1, 2) < (1, 2))
print((1, 2, 3) < (1, 2, 3, 4))
print((1, 2) == (1, 2))
print((1, 2) != (1, 3))
# Membership
print(2 in (1, 2, 3))
print(4 in (1, 2, 3))
# Single element tuple
print((1,))
# Empty tuple
print(())

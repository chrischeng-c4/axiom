// top_level.rs — `.dist-info/top_level.txt` reader and writer.
//
// `top_level.txt` is the pre-PEP 427 listing of top-level importable
// module names shipped by a distribution. It's been superseded by
// reading `RECORD` directly (everything in `RECORD` whose path is
// `pkg/__init__.py` or `module.py` at depth 1 is a top-level), but pip
// + setuptools still emit it, and uv preserves it on uninstall to know
// which top-levels to remove when no other distribution claims them.
//
// Format: one name per line. Blank lines tolerated. setuptools
// occasionally adds `#`-prefixed comment lines on regenerate; we drop
// them. Order is preserved on output to match what was written.

/// Parse the body of a `top_level.txt` file. Returns names in source
/// order with duplicates removed (first occurrence wins).
pub fn parse_top_level(src: &str) -> Vec<String> {
    let mut out: Vec<String> = Vec::new();
    let mut seen: std::collections::BTreeSet<String> = Default::default();
    for raw in src.lines() {
        let name = match raw.split('#').next() {
            Some(s) => s.trim(),
            None => raw.trim(),
        };
        if name.is_empty() {
            continue;
        }
        if seen.insert(name.to_string()) {
            out.push(name.to_string());
        }
    }
    out
}

/// Render a top-level list as `.dist-info/top_level.txt` body. One name
/// per line, terminated with a single `\n`. Order is preserved.
pub fn render_top_level(names: &[String]) -> String {
    let mut out = String::with_capacity(names.iter().map(|s| s.len() + 1).sum::<usize>());
    for n in names {
        out.push_str(n);
        out.push('\n');
    }
    out
}

#[cfg(test)]
mod tests {
    use super::*;

    fn v(items: &[&str]) -> Vec<String> {
        items.iter().map(|s| (*s).to_string()).collect()
    }

    #[test]
    fn parses_single_name() {
        assert_eq!(parse_top_level("spam\n"), v(&["spam"]));
    }

    #[test]
    fn parses_multiple_names() {
        assert_eq!(parse_top_level("spam\neggs\n"), v(&["spam", "eggs"]));
    }

    #[test]
    fn skips_blank_lines() {
        assert_eq!(parse_top_level("spam\n\neggs\n\n"), v(&["spam", "eggs"]));
    }

    #[test]
    fn skips_comment_lines() {
        let src = "# generated by setuptools\nspam\n# eggs (commented out)\neggs\n";
        assert_eq!(parse_top_level(src), v(&["spam", "eggs"]));
    }

    #[test]
    fn strips_inline_comments() {
        // setuptools occasionally appends a `# pkg` annotation on the
        // same line as the name. Strip it.
        assert_eq!(parse_top_level("spam  # top-level\n"), v(&["spam"]));
    }

    #[test]
    fn dedups_preserving_first_occurrence_order() {
        let src = "spam\neggs\nspam\nham\neggs\n";
        assert_eq!(parse_top_level(src), v(&["spam", "eggs", "ham"]));
    }

    #[test]
    fn tolerates_no_trailing_newline() {
        assert_eq!(parse_top_level("spam"), v(&["spam"]));
    }

    #[test]
    fn empty_input_yields_empty_vec() {
        assert!(parse_top_level("").is_empty());
    }

    #[test]
    fn whitespace_only_input_yields_empty_vec() {
        assert!(parse_top_level("\n   \n\t\n").is_empty());
    }

    #[test]
    fn render_one_name() {
        assert_eq!(render_top_level(&v(&["spam"])), "spam\n");
    }

    #[test]
    fn render_multi_name_preserves_order() {
        assert_eq!(
            render_top_level(&v(&["zlib", "alfa", "mike"])),
            "zlib\nalfa\nmike\n"
        );
    }

    #[test]
    fn render_empty_list_yields_empty_string() {
        assert_eq!(render_top_level(&[]), "");
    }

    #[test]
    fn round_trip_canonicalizes() {
        let messy = "# comment\nspam\n\nspam\neggs\n# end\n";
        let parsed = parse_top_level(messy);
        let rendered = render_top_level(&parsed);
        // After round-trip we have the canonical form: deduped, no
        // comments, no blanks, single trailing newline.
        assert_eq!(rendered, "spam\neggs\n");
        // And it's idempotent — re-parsing yields the same list.
        assert_eq!(parse_top_level(&rendered), parsed);
    }

    #[test]
    fn name_with_dots_is_preserved_verbatim() {
        // Modern wheels typically ship one top-level per dot-segment
        // (`a` for `a.b`), but we don't enforce — we pass through.
        assert_eq!(parse_top_level("foo.bar\n"), v(&["foo.bar"]));
    }
}

// pth_file.rs — `.pth` site-packages path-configuration file parser.
//
// A `.pth` file dropped into a site-packages directory is processed by
// CPython's site.py at interpreter start-up. Each line is one of:
//
//   * blank or whitespace-only — skipped.
//   * `#`-prefixed — comment, skipped.
//   * `import …` or `import\t…` (case-sensitive `import`, followed by
//     whitespace) — executed as Python code. setuptools/pip emit these
//     for namespace-package init shims; uv emits one for every editable
//     install pointing into the source tree.
//   * anything else — interpreted as a directory path (absolute or
//     relative to the site dir) and appended to `sys.path` if it
//     exists.
//
// mamba doesn't execute arbitrary Python at parse time. This module
// returns a structured list of entries so the venv / editable / site
// layers can decide what to do with each one (mount the path, queue
// the import for the runtime, drop the line on round-trip, etc.).
//
// References:
//   * CPython Lib/site.py — `addpackage` (the file processor).
//   * PEP 405 — virtual environments.
//   * uv `src/uv-install-wheel/src/script.rs` — pip parity reference.

use crate::pkgmanage::pkgmgr::types::IndexError;

/// One classified line from a `.pth` file.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum PthEntry {
    /// A path to add to `sys.path`. Empty / whitespace-only paths are
    /// filtered out by the parser; the value here is the trimmed
    /// directory string as written by the producer.
    Path(String),
    /// An `import …` statement to be executed by site.py. The whole
    /// line is preserved (including the leading `import` keyword) so
    /// that callers can hand the string straight to the Python runtime
    /// if they want to honor it.
    Import(String),
}

/// Parse a `.pth` file body. Returns entries in source order; blank
/// and comment lines are dropped.
pub fn parse_pth(src: &str) -> Result<Vec<PthEntry>, IndexError> {
    let mut out = Vec::new();
    for (lineno, raw) in src.lines().enumerate() {
        let trimmed = raw.trim();
        if trimmed.is_empty() || trimmed.starts_with('#') {
            continue;
        }
        if is_import_line(trimmed) {
            out.push(PthEntry::Import(trimmed.to_string()));
            continue;
        }
        // Any other line is a path. Reject embedded NUL — site.py
        // would carry it into sys.path as-is, but it's a near-certain
        // producer bug and easy to surface here.
        if trimmed.contains('\0') {
            return Err(IndexError::ParseError {
                url: ".pth".into(),
                detail: format!("line {n}: NUL byte in path", n = lineno + 1),
            });
        }
        out.push(PthEntry::Path(trimmed.to_string()));
    }
    Ok(out)
}

/// Render a list of entries back to `.pth` body form. One entry per
/// line, terminated with `\n`. Paths are emitted verbatim; import
/// directives keep their leading `import …` form.
pub fn render_pth(entries: &[PthEntry]) -> String {
    let mut out = String::new();
    for e in entries {
        match e {
            PthEntry::Path(p) => {
                out.push_str(p);
                out.push('\n');
            }
            PthEntry::Import(line) => {
                out.push_str(line);
                out.push('\n');
            }
        }
    }
    out
}

/// site.py's classifier: a line is treated as an import directive iff
/// it starts with the case-sensitive token `import` followed by ASCII
/// whitespace. `imports`, `Import`, `import(` etc. are treated as
/// paths, matching CPython behavior verbatim.
fn is_import_line(s: &str) -> bool {
    let rest = match s.strip_prefix("import") {
        Some(r) => r,
        None => return false,
    };
    matches!(rest.chars().next(), Some(c) if c == ' ' || c == '\t')
}

#[cfg(test)]
mod tests {
    use super::*;

    fn p(s: &str) -> PthEntry {
        PthEntry::Path(s.to_string())
    }
    fn i(s: &str) -> PthEntry {
        PthEntry::Import(s.to_string())
    }

    #[test]
    fn parses_single_path() {
        assert_eq!(parse_pth("/opt/site\n").unwrap(), vec![p("/opt/site")]);
    }

    #[test]
    fn parses_multiple_paths() {
        let src = "/opt/site\n../local/lib\n";
        assert_eq!(parse_pth(src).unwrap(), vec![p("/opt/site"), p("../local/lib")]);
    }

    #[test]
    fn skips_blank_lines() {
        let src = "\n/opt/site\n\n\n/etc\n\n";
        assert_eq!(parse_pth(src).unwrap(), vec![p("/opt/site"), p("/etc")]);
    }

    #[test]
    fn skips_whitespace_only_lines() {
        let src = "   \n\t\n/opt/site\n";
        assert_eq!(parse_pth(src).unwrap(), vec![p("/opt/site")]);
    }

    #[test]
    fn skips_comment_lines() {
        let src = "# generated by uv\n/opt/site\n# end\n";
        assert_eq!(parse_pth(src).unwrap(), vec![p("/opt/site")]);
    }

    #[test]
    fn parses_import_directive_with_space() {
        assert_eq!(
            parse_pth("import sitecustomize\n").unwrap(),
            vec![i("import sitecustomize")]
        );
    }

    #[test]
    fn parses_import_directive_with_tab() {
        assert_eq!(
            parse_pth("import\tsitecustomize\n").unwrap(),
            vec![i("import\tsitecustomize")]
        );
    }

    #[test]
    fn import_with_no_following_whitespace_is_a_path() {
        // `imports/` is a directory name, not an import directive.
        assert_eq!(
            parse_pth("imports/local\n").unwrap(),
            vec![p("imports/local")]
        );
    }

    #[test]
    fn import_capital_i_is_a_path() {
        // site.py matches case-sensitively on `import`.
        assert_eq!(
            parse_pth("Import sitecustomize\n").unwrap(),
            vec![p("Import sitecustomize")]
        );
    }

    #[test]
    fn mixed_paths_and_imports_preserve_order() {
        let src = "import _virtualenv\n/opt/site\nimport sitecustomize\n../rel\n";
        assert_eq!(
            parse_pth(src).unwrap(),
            vec![
                i("import _virtualenv"),
                p("/opt/site"),
                i("import sitecustomize"),
                p("../rel"),
            ]
        );
    }

    #[test]
    fn trims_surrounding_whitespace_on_paths() {
        let src = "   /opt/site   \n";
        assert_eq!(parse_pth(src).unwrap(), vec![p("/opt/site")]);
    }

    #[test]
    fn trims_surrounding_whitespace_on_imports() {
        let src = "   import sitecustomize   \n";
        assert_eq!(parse_pth(src).unwrap(), vec![i("import sitecustomize")]);
    }

    #[test]
    fn tolerates_no_trailing_newline() {
        assert_eq!(parse_pth("/opt/site").unwrap(), vec![p("/opt/site")]);
    }

    #[test]
    fn empty_input_yields_empty_vec() {
        assert!(parse_pth("").unwrap().is_empty());
    }

    #[test]
    fn rejects_nul_in_path() {
        let err = parse_pth("/opt/\0bad\n").unwrap_err();
        assert!(err.to_string().contains("NUL byte"));
    }

    #[test]
    fn nul_error_reports_one_based_line_number() {
        let err = parse_pth("/opt/ok\n\n/bad/\0path\n").unwrap_err();
        assert!(err.to_string().contains("line 3"));
    }

    #[test]
    fn render_single_path() {
        assert_eq!(render_pth(&[p("/opt/site")]), "/opt/site\n");
    }

    #[test]
    fn render_import_preserves_form() {
        assert_eq!(
            render_pth(&[i("import sitecustomize")]),
            "import sitecustomize\n"
        );
    }

    #[test]
    fn render_empty_yields_empty() {
        assert_eq!(render_pth(&[]), "");
    }

    #[test]
    fn round_trip_drops_comments_and_blanks() {
        let messy = "# generated\n\nimport _virtualenv\n  /opt/site  \n# end\n";
        let parsed = parse_pth(messy).unwrap();
        let rendered = render_pth(&parsed);
        // Round-trip canonicalises: comments / blanks gone, paths
        // trimmed, single trailing newline per entry.
        assert_eq!(rendered, "import _virtualenv\n/opt/site\n");
        // And parsing the rendered form yields the same vec.
        assert_eq!(parse_pth(&rendered).unwrap(), parsed);
    }

    #[test]
    fn relative_path_preserved_verbatim() {
        // site.py resolves relatives against the containing dir; we
        // keep the string as-written for the venv layer to resolve.
        assert_eq!(
            parse_pth("../../shared/lib\n").unwrap(),
            vec![p("../../shared/lib")]
        );
    }

    #[test]
    fn windows_style_path_preserved() {
        // Backslashes are legal in Windows site-packages paths; pass
        // through.
        let src = r"C:\Python312\Lib\site-packages\foo";
        assert_eq!(parse_pth(src).unwrap(), vec![p(src)]);
    }

    #[test]
    fn url_like_path_is_still_treated_as_path() {
        // .pth files don't support URLs, but if a producer wrote one
        // we still surface it as a path entry — callers decide.
        assert_eq!(
            parse_pth("https://example.com/lib\n").unwrap(),
            vec![p("https://example.com/lib")]
        );
    }
}

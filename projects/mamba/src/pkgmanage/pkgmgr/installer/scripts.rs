use std::fs;
use std::path::Path;

use super::InstallerError;

#[derive(Debug, Clone, PartialEq, Eq)]
struct EntryPoint {
    name: String,
    module: String,
    attr: Option<String>,
}

/// Parse the `[console_scripts]` group out of an `entry_points.txt` body. Other
/// groups (e.g. `[gui_scripts]`) are ignored in Phase-1.3.
fn parse_console_scripts(text: &str) -> Vec<EntryPoint> {
    let mut out = Vec::new();
    let mut in_group = false;
    for raw in text.lines() {
        let line = raw.trim();
        if line.is_empty() || line.starts_with('#') || line.starts_with(';') {
            continue;
        }
        if let Some(group) = line.strip_prefix('[').and_then(|s| s.strip_suffix(']')) {
            in_group = group.trim() == "console_scripts";
            continue;
        }
        if !in_group {
            continue;
        }
        let Some((name, target)) = line.split_once('=') else {
            continue;
        };
        let name = name.trim().to_string();
        let target = target.trim();
        if name.is_empty() || target.is_empty() {
            continue;
        }
        let (module, attr) = match target.split_once(':') {
            Some((m, a)) => (m.trim().to_string(), Some(a.trim().to_string())),
            None => (target.to_string(), None),
        };
        out.push(EntryPoint { name, module, attr });
    }
    out
}

/// Generate `bin/<name>` shebang wrappers for every `[console_scripts]` entry.
/// Returns the list of script names that were written.
pub fn write_console_scripts(
    entry_points_text: &str,
    bin_dir: &Path,
    python_executable: &Path,
) -> Result<Vec<String>, InstallerError> {
    let scripts = parse_console_scripts(entry_points_text);
    if scripts.is_empty() {
        return Ok(Vec::new());
    }

    fs::create_dir_all(bin_dir).map_err(|e| InstallerError::Io {
        path: Some(bin_dir.to_path_buf()),
        detail: e.to_string(),
    })?;

    let mut names = Vec::with_capacity(scripts.len());
    for ep in scripts {
        let body = render_wrapper(&ep, python_executable);
        let dest = bin_dir.join(&ep.name);
        fs::write(&dest, body).map_err(|e| InstallerError::Io {
            path: Some(dest.clone()),
            detail: e.to_string(),
        })?;
        #[cfg(unix)]
        {
            use std::os::unix::fs::PermissionsExt;
            fs::set_permissions(&dest, fs::Permissions::from_mode(0o755)).map_err(|e| {
                InstallerError::Io {
                    path: Some(dest.clone()),
                    detail: e.to_string(),
                }
            })?;
        }
        names.push(ep.name);
    }
    Ok(names)
}

fn render_wrapper(ep: &EntryPoint, python_executable: &Path) -> String {
    let py = python_executable.display();
    let module = &ep.module;
    let import_call = match ep.attr.as_deref() {
        Some(attr) => format!(
            "import sys\nfrom {module} import {attr}\nsys.exit({attr}())\n",
            module = module,
            attr = attr,
        ),
        None => format!(
            "import runpy\nrunpy.run_module('{module}', run_name='__main__')\n",
            module = module,
        ),
    };
    format!(
        "#!{py}\n# -*- coding: utf-8 -*-\n# Auto-generated by mamba installer.\n{import_call}",
        py = py,
        import_call = import_call,
    )
}

#[cfg(test)]
mod tests {
    use super::*;
    use std::path::PathBuf;

    #[test]
    fn parses_console_scripts_only() {
        let text = "\
[console_scripts]
httpie = httpie.core:main
http = httpie.core:main

[gui_scripts]
gui = some.gui:run
";
        let parsed = parse_console_scripts(text);
        assert_eq!(parsed.len(), 2);
        assert_eq!(parsed[0].name, "httpie");
        assert_eq!(parsed[0].module, "httpie.core");
        assert_eq!(parsed[0].attr.as_deref(), Some("main"));
    }

    #[test]
    fn renders_wrapper_with_attr() {
        let ep = EntryPoint {
            name: "httpie".into(),
            module: "httpie.core".into(),
            attr: Some("main".into()),
        };
        let body = render_wrapper(&ep, &PathBuf::from("/usr/bin/python3"));
        assert!(body.starts_with("#!/usr/bin/python3\n"));
        assert!(body.contains("from httpie.core import main"));
        assert!(body.contains("sys.exit(main())"));
    }

    #[test]
    fn renders_wrapper_module_only() {
        let ep = EntryPoint {
            name: "pkg".into(),
            module: "pkg.cli".into(),
            attr: None,
        };
        let body = render_wrapper(&ep, &PathBuf::from("/usr/bin/python3"));
        assert!(body.contains("runpy.run_module('pkg.cli'"));
    }
}

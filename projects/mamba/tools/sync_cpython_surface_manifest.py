#!/usr/bin/env python3
"""Generate mechanical surface manifests from CPython 3.12 API data.

The source of truth is ``data/cpython312_surface.json``. This tool projects
each ``module.name`` entry into one attr-existence fixture so the CPython API
surface has an explicit, one-case-per-file test row.

By default it prints what would be written. Use ``--write`` to update generated
manifests and ``--generate`` to run ``fixture_gen`` for those manifests.
"""

from __future__ import annotations

import argparse
import importlib.util
import json
import re
import sys
from pathlib import Path

TOOLS_DIR = Path(__file__).resolve().parent
MAMBA_DIR = TOOLS_DIR.parent
SURFACE_JSON = MAMBA_DIR / "data" / "cpython312_surface.json"
OUT_DIR = (
    MAMBA_DIR
    / "tests"
    / "cpython"
    / "config"
    / "manifests"
    / "std-libs"
    / "cpython312_surface"
)


def lib_name(module: str) -> str:
    return module.replace(".", "_").lower()


def snake(name: str) -> str:
    text = re.sub(r"(.)([A-Z][a-z]+)", r"\1_\2", name)
    text = re.sub(r"([a-z0-9])([A-Z])", r"\1_\2", text)
    text = text.replace("__", "_dunder_")
    text = re.sub(r"[^A-Za-z0-9]+", "_", text)
    text = re.sub(r"_+", "_", text).strip("_").lower()
    return text or "symbol"


def case_name(name: str, used: set[str]) -> str:
    base = f"api_{snake(name)}_is_present"
    case = base
    i = 2
    while case in used:
        case = f"{base}_{i}"
        i += 1
    used.add(case)
    return case


def q(value: str) -> str:
    return json.dumps(value)


def import_module_for_subject(module: str, name: str) -> str:
    """Import the child module when a surface subject is a package submodule."""
    subject = f"{module}.{name}"
    try:
        spec = importlib.util.find_spec(subject)
    except (ImportError, AttributeError, ValueError):
        spec = None
    return subject if spec is not None else module


def render_manifest(module: str, names: list[str]) -> str:
    lib = lib_name(module)
    used: set[str] = set()
    lines = [
        f"# Generated by tools/sync_cpython_surface_manifest.py from {SURFACE_JSON.name}.",
        "# Do not hand-edit; update the source JSON or the generator instead.",
        "",
        'bucket = "std-libs"',
        f"lib = {q(lib)}",
        "",
    ]
    for name in names:
        subject = f"{module}.{name}"
        import_module = import_module_for_subject(module, name)
        lines.extend(
            [
                "[[case]]",
                'dimension = "surface"',
                f"case      = {q(case_name(name, used))}",
                f"subject   = {q(subject)}",
                'kind      = "mechanical"',
                'probe     = "attr"',
                f"attr      = {q(name)}",
                f"expr      = {q(module)}",
                f"module    = {q(import_module)}",
                f"source    = {q('projects/mamba/data/cpython312_surface.json')}",
                "",
            ]
        )
    return "\n".join(lines)


def load_surface() -> dict[str, list[str]]:
    data = json.loads(SURFACE_JSON.read_text(encoding="utf-8"))
    modules = data["modules"]
    out: dict[str, list[str]] = {}
    total = 0
    for module in sorted(modules):
        names = list(modules[module]["names"])
        total += len(names)
        out[module] = names
    expected = int(data["total_name_count"])
    if total != expected:
        raise SystemExit(
            f"{SURFACE_JSON}: total_name_count={expected} but modules contain {total}"
        )
    return out


def write_manifests(modules: dict[str, list[str]], *, write: bool) -> list[Path]:
    paths: list[Path] = []
    for module, names in modules.items():
        path = OUT_DIR / f"{lib_name(module)}.toml"
        paths.append(path)
        body = render_manifest(module, names)
        if write:
            path.parent.mkdir(parents=True, exist_ok=True)
            path.write_text(body, encoding="utf-8")
    return paths


def generate_fixtures(paths: list[Path]) -> None:
    import fixture_gen

    totals = {"create": 0, "regenerate": 0, "skip-filled": 0}
    for path in paths:
        for _out, action in fixture_gen.process_manifest(path):
            totals[action] += 1
    print(
        "fixtures: "
        f"created={totals['create']} "
        f"regenerated={totals['regenerate']} "
        f"skipped-filled={totals['skip-filled']}"
    )


def main(argv: list[str] | None = None) -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--write", action="store_true", help="write generated manifests")
    ap.add_argument(
        "--generate",
        action="store_true",
        help="run fixture_gen for generated manifests; implies --write",
    )
    args = ap.parse_args(argv)

    modules = load_surface()
    write = bool(args.write or args.generate)
    paths = write_manifests(modules, write=write)
    total_names = sum(len(names) for names in modules.values())
    mode = "wrote" if write else "would write"
    print(f"{mode} {len(paths)} manifests covering {total_names} API names")
    if args.generate:
        generate_fixtures(paths)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

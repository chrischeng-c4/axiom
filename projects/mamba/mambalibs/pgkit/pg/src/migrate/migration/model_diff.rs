//! Introspect model definitions and diff against the live DB schema.
//!
//! Delegates to [`AutoDetector`] for schema comparison, then filters the
//! generated SQL to the allowed scope:
//!
//! | Scope    | Included |
//! |----------|----------|
//! | Tables   | CREATE TABLE / DROP TABLE |
//! | Columns  | ADD COLUMN / DROP COLUMN / ALTER COLUMN |
//! | Indexes  | CREATE INDEX / DROP INDEX |
//! | **Excluded** | FOREIGN KEY constraints, CHECK constraints |
//!
//! The output is written to a `.sql` file under `migrations/` following the
//! `-- UP` / `-- DOWN` format consumed by [`MigrationRunner`].

use crate::{
    auto_detect::{AutoDetectConfig, AutoDetector, ModelDefinition},
    Connection, DataBridgeError, Result,
};
use chrono::Utc;
use std::fs;
use std::path::Path;

// -- ModelDiffResult ----------------------------------------------------------

/// Result of diffing models against the live DB schema.
#[derive(Debug, Clone)]
pub struct ModelDiffResult {
    /// SQL to bring the DB to the desired state (table/column/index only).
    pub up_sql: String,
    /// SQL to reverse the changes.
    pub down_sql: String,
    /// Human-readable summary of changes (table/column/index only).
    pub summary: Vec<String>,
    /// `true` if there are changes to apply.
    pub has_changes: bool,
}

// -- ModelDiffer --------------------------------------------------------------

/// Diffs a set of model definitions against the live database schema and
/// generates scoped SQL migration files.
pub struct ModelDiffer {
    conn: Connection,
}

impl ModelDiffer {
    /// Create from an existing connection.
    pub fn new(conn: Connection) -> Self {
        Self { conn }
    }

    /// Detect schema differences between `models` and the live DB.
    ///
    /// Scope is limited to table, column, and index changes.
    /// Foreign key and check constraints are excluded.
    pub async fn diff(&self, models: &[ModelDefinition]) -> Result<ModelDiffResult> {
        let config = AutoDetectConfig {
            exclude_tables: vec![
                "_migrations".to_string(),
                "alembic_version".to_string(),
            ],
            ..AutoDetectConfig::default()
        };

        let detector = AutoDetector::with_config(self.conn.clone(), config);
        let result = detector.detect(models).await?;

        // Filter FK / constraint DDL from both SQL blocks
        let up_sql = filter_scope(&result.up_sql);
        let down_sql = filter_scope(&result.down_sql);

        // Filter summary lines that mention FK
        let summary: Vec<String> = result
            .summary
            .iter()
            .filter(|s| {
                let u = s.to_uppercase();
                !u.contains("FOREIGN KEY") && !u.contains("ADD FOREIGN") && !u.contains("DROP FOREIGN")
            })
            .cloned()
            .collect();

        let has_changes = !up_sql.trim().is_empty();

        Ok(ModelDiffResult { up_sql, down_sql, summary, has_changes })
    }

    /// Write the diff result to a new `.sql` file in `output_dir`.
    ///
    /// File name format: `<timestamp>_<message>.sql`
    ///
    /// Returns the generated filename on success.
    pub fn write_migration_file(
        &self,
        result: &ModelDiffResult,
        message: &str,
        output_dir: &Path,
    ) -> Result<String> {
        if !result.has_changes {
            return Err(DataBridgeError::Validation(
                "No schema changes detected".to_string(),
            ));
        }

        fs::create_dir_all(output_dir).map_err(|e| {
            DataBridgeError::Internal(format!("create migrations dir: {e}"))
        })?;

        let timestamp = Utc::now().format("%Y%m%d_%H%M%S");
        let slug = message.replace(' ', "_").to_lowercase();
        let filename = format!("{}_{}.sql", timestamp, slug);
        let filepath = output_dir.join(&filename);

        let mut content = String::new();
        content.push_str(&format!("-- Migration: {}_{}\n", timestamp, message));
        content.push_str(&format!("-- Description: {}\n", message));
        content.push_str("-- Auto-generated by cclab pg migrate generate\n");
        content.push_str("--\n-- Changes:\n");
        for s in &result.summary {
            content.push_str(&format!("--   {}\n", s));
        }
        content.push_str("\n-- UP\n");
        content.push_str(result.up_sql.trim());
        content.push_str("\n\n-- DOWN\n");
        content.push_str(result.down_sql.trim());
        content.push('\n');

        fs::write(&filepath, &content).map_err(|e| {
            DataBridgeError::Internal(format!("write {}: {e}", filepath.display()))
        })?;

        Ok(filename)
    }
}

// -- SQL filtering ------------------------------------------------------------

/// Remove any SQL statements that add or drop FOREIGN KEY constraints.
///
/// The filter operates at the statement level (split on `;`). A statement is
/// excluded if it contains `FOREIGN KEY` or `REFERENCES` as a standalone
/// ALTER TABLE constraint operation.
fn filter_scope(sql: &str) -> String {
    let mut out = Vec::new();

    for stmt in sql.split(';') {
        let trimmed = stmt.trim();
        if trimmed.is_empty() {
            continue;
        }
        let upper = trimmed.to_uppercase();
        // Drop ALTER TABLE ... ADD CONSTRAINT ... FOREIGN KEY
        if upper.contains("FOREIGN KEY") {
            continue;
        }
        // Drop ALTER TABLE ... DROP CONSTRAINT (FK cleanup)
        if upper.contains("DROP CONSTRAINT") && upper.contains("FK") {
            continue;
        }
        out.push(format!("{};", trimmed));
    }

    out.join("\n")
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn filter_removes_fk_statements() {
        let sql = "\
            ALTER TABLE users ADD COLUMN email TEXT;\n\
            ALTER TABLE users ADD CONSTRAINT fk_users_org FOREIGN KEY (org_id) REFERENCES orgs(id);\n\
            CREATE INDEX idx_users_email ON users(email);\
        ";
        let filtered = filter_scope(sql);
        assert!(filtered.contains("ADD COLUMN email"));
        assert!(filtered.contains("CREATE INDEX"));
        assert!(!filtered.contains("FOREIGN KEY"));
    }

    #[test]
    fn filter_keeps_create_table() {
        let sql = "CREATE TABLE users (id SERIAL PRIMARY KEY, name TEXT NOT NULL);";
        let filtered = filter_scope(sql);
        assert!(filtered.contains("CREATE TABLE users"));
    }

    #[test]
    fn filter_handles_empty_input() {
        assert_eq!(filter_scope(""), "");
        assert_eq!(filter_scope("   \n\n  "), "");
    }

    #[test]
    fn generate_new_column_migration() {
        let result = ModelDiffResult {
            up_sql: "ALTER TABLE users ADD COLUMN email TEXT NOT NULL;".to_string(),
            down_sql: "ALTER TABLE users DROP COLUMN email;".to_string(),
            summary: vec![],
            has_changes: true,
        };
        let up_filtered = filter_scope(&result.up_sql);
        let down_filtered = filter_scope(&result.down_sql);
        assert!(up_filtered.contains("ADD COLUMN email"));
        assert!(down_filtered.contains("DROP COLUMN email"));
        assert!(!up_filtered.contains("FOREIGN KEY"));
    }

    #[test]
    fn generate_new_table_migration() {
        let result = ModelDiffResult {
            up_sql: "CREATE TABLE orders (id SERIAL PRIMARY KEY, total NUMERIC);".to_string(),
            down_sql: "DROP TABLE orders;".to_string(),
            summary: vec![],
            has_changes: true,
        };
        let up_filtered = filter_scope(&result.up_sql);
        let down_filtered = filter_scope(&result.down_sql);
        assert!(up_filtered.contains("CREATE TABLE orders"));
        assert!(down_filtered.contains("DROP TABLE orders"));
    }

    #[test]
    fn generate_new_index_migration() {
        let result = ModelDiffResult {
            up_sql: "CREATE INDEX idx_users_email ON users(email);".to_string(),
            down_sql: "DROP INDEX IF EXISTS idx_users_email;".to_string(),
            summary: vec![],
            has_changes: true,
        };
        let up_filtered = filter_scope(&result.up_sql);
        let down_filtered = filter_scope(&result.down_sql);
        assert!(up_filtered.contains("CREATE INDEX idx_users_email"));
        assert!(down_filtered.contains("DROP INDEX"));
    }

    #[test]
    fn no_changes_has_changes_false() {
        let result = ModelDiffResult {
            up_sql: "".to_string(),
            down_sql: "".to_string(),
            summary: vec![],
            has_changes: false,
        };
        assert!(!result.has_changes);
    }
}

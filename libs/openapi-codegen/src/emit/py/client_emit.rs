//! Emit an `httpx`-based Python client: one method per operation, with
//! pydantic-validated responses.

use crate::emit::py::pymap;
use crate::ir::names::{to_snake, NameRegistry};
use crate::ir::openapi::RefOr;
use crate::ir::operations::{OperationIR, ParamIR};
use crate::ir::typemap::TypeMap;

pub const HEADER: &str = "# Code generated by openapi-codegen. DO NOT EDIT.\n";

pub fn emit(ops: &[OperationIR], tm: &TypeMap) -> String {
    let mut out = String::from(HEADER);
    out.push_str("from __future__ import annotations\n");
    out.push_str("from typing import Any, Literal, Optional\n");
    out.push_str("import httpx\n");
    out.push_str("from pydantic import BaseModel\n");
    out.push_str("from .models import *  # noqa: F401,F403\n\n\n");

    out.push_str("class Client:\n");
    out.push_str("    def __init__(self, base_url: str, *, client: Optional[httpx.Client] = None) -> None:\n");
    out.push_str("        self._base_url = base_url.rstrip(\"/\")\n");
    out.push_str("        self._client = client or httpx.Client()\n");

    let mut reg = NameRegistry::new();
    reg.unique("__init__");
    for ir in ops {
        out.push('\n');
        out.push_str(&emit_method(ir, tm, &mut reg));
    }
    out
}

fn fn_name(ir: &OperationIR) -> String {
    match &ir.operation_id {
        Some(id) if !id.trim().is_empty() => to_snake(id),
        _ => to_snake(&format!(
            "{}_{}",
            ir.method,
            ir.path.replace(['/', '{', '}'], "_")
        )),
    }
}

/// `(python_param, type, required)` for a parameter.
fn param_ann(p: &ParamIR, tm: &TypeMap) -> (String, String, bool) {
    let ty = p
        .schema
        .as_ref()
        .map(|s| pymap::type_expr(s, tm))
        .unwrap_or_else(|| "str".to_string());
    (to_snake(&p.name), ty, p.required)
}

fn emit_method(ir: &OperationIR, tm: &TypeMap, reg: &mut NameRegistry) -> String {
    let name = reg.unique(&fn_name(ir));

    // Signature parameters in a stable order: path, query, header, body.
    let mut sig: Vec<String> = Vec::new();
    let path_anns: Vec<(String, String, bool)> =
        ir.path_params.iter().map(|p| param_ann(p, tm)).collect();
    let query_anns: Vec<(String, String, bool)> =
        ir.query_params.iter().map(|p| param_ann(p, tm)).collect();
    let header_anns: Vec<(String, String, bool)> =
        ir.header_params.iter().map(|p| param_ann(p, tm)).collect();

    for (n, t, _) in &path_anns {
        sig.push(format!("{n}: {t}")); // path params are always required
    }
    for (n, t, req) in query_anns.iter().chain(header_anns.iter()) {
        if *req {
            sig.push(format!("{n}: {t}"));
        } else {
            sig.push(format!("{n}: {} = None", pymap::optional(t)));
        }
    }
    let body_ty = ir.body.as_ref().map(|b| pymap::type_expr(&b.schema, tm));
    if let Some(ty) = &body_ty {
        let required = ir.body.as_ref().map(|b| b.required).unwrap_or(false);
        if required {
            sig.push(format!("body: {ty}"));
        } else {
            sig.push(format!("body: {} = None", pymap::optional(ty)));
        }
    }

    let (ret_ann, ret_stmt) = response(ir, tm);
    let sig_str = if sig.is_empty() {
        String::new()
    } else {
        format!(", *, {}", sig.join(", "))
    };

    let mut m = format!("    def {name}(self{sig_str}) -> {ret_ann}:\n");

    // Path with python param names substituted into the f-string.
    let mut path = ir.path.clone();
    for (p, (snake, ..)) in ir.path_params.iter().zip(&path_anns) {
        path = path.replace(&format!("{{{}}}", p.name), &format!("{{{snake}}}"));
    }
    m.push_str(&format!("        _path = f\"{path}\"\n"));

    m.push_str("        _params: dict[str, Any] = {}\n");
    for (p, (snake, _, req)) in ir.query_params.iter().zip(&query_anns) {
        if *req {
            m.push_str(&format!(
                "        _params[\"{}\"] = {snake}\n",
                esc(&p.name)
            ));
        } else {
            m.push_str(&format!(
                "        if {snake} is not None:\n            _params[\"{}\"] = {snake}\n",
                esc(&p.name)
            ));
        }
    }
    m.push_str("        _headers: dict[str, Any] = {}\n");
    for (p, (snake, _, req)) in ir.header_params.iter().zip(&header_anns) {
        if *req {
            m.push_str(&format!(
                "        _headers[\"{}\"] = {snake}\n",
                esc(&p.name)
            ));
        } else {
            m.push_str(&format!(
                "        if {snake} is not None:\n            _headers[\"{}\"] = {snake}\n",
                esc(&p.name)
            ));
        }
    }

    let json_arg = if body_ty.is_some() {
        m.push_str("        _json = body.model_dump(by_alias=True, exclude_none=True) if isinstance(body, BaseModel) else body\n");
        ", json=_json"
    } else {
        ""
    };

    m.push_str(&format!(
        "        _resp = self._client.request(\"{}\", self._base_url + _path, params=_params, headers=_headers{json_arg})\n",
        ir.http_method
    ));
    m.push_str("        _resp.raise_for_status()\n");
    m.push_str(&format!("        {ret_stmt}\n"));
    m
}

/// `(return annotation, return statement)` for the operation's response.
fn response(ir: &OperationIR, tm: &TypeMap) -> (String, String) {
    match &ir.response {
        None => ("None".to_string(), "return None".to_string()),
        Some(RefOr::Ref(r)) => match tm.resolve_ref(&r.reference) {
            Some(model) => (
                model.clone(),
                format!("return {model}.model_validate(_resp.json())"),
            ),
            None => ("Any".to_string(), "return _resp.json()".to_string()),
        },
        Some(node @ RefOr::Item(_)) => (
            pymap::type_expr(node, tm),
            "return _resp.json()".to_string(),
        ),
    }
}

fn esc(s: &str) -> String {
    s.replace('\\', "\\\\").replace('"', "\\\"")
}
